    <h3>Расширяемость</h3>
    <p>HTML имеет широкий спектр механизмов расширяемости, которые можно использовать для безопасного добавления семантики:</p>
    <ul>
        <li>Авторы могут использовать атрибут <code>class</code> для расширения элементов, эффективно создавая свои собственные элементы, используя наиболее подходящий существующий &quot;настоящий&quot; HTML-элемент, так что браузеры и другие инструменты, которые не знают о расширении, могут поддерживать его в некоторой степени хорошо. Такую способность используют, например, микроформаты.</li>
        <li>Авторы могут включать данные для встроенных клиентских скриптов или серверных скриптов для всего сайта для обработки с использованием атрибутов <code>data-*=""</code>. Они гарантированно никогда не будут затронуты браузерами и позволяют скриптам включать данные в элементы HTML, которые затем скрипты могут искать и обрабатывать.</li>
        <li>Авторы могут использовать механизм <code>&lt;meta name="" content=""&gt;</code> для включения метаданных всей страницы.</li>
        <li>Авторы могут использовать механизм <code>rel=""</code> для аннотирования ссылок с определенным значением путем регистрации расширений для <a href="https://html.spec.whatwg.org/dev/links.html#concept-rel-extensions" target="_blank" rel="nofollow">предопределенного набора типов ссылок</a>. Это также используется микроформатами.</li>
        <li>Авторы могут встраивать необработанные данные с помощью механизма <code>&lt;script type=""&gt;</code> с настраиваемым типом для дальнейшей обработки встроенными или серверными скриптами.</li>
        <li>Авторы могут расширять API, используя механизм прототипирования JavaScript. Например, это широко используется библиотеками скриптов.</li>
        <li>Авторы могут использовать функцию микроданных (атрибуты <code>itemscope=""</code> и <code>itemprop=""</code>) для встраивания вложенных пар &quot;имя-значение&quot; данных для совместного использования с другими приложениями и сайтами.</li>
    </ul>

