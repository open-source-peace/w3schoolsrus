    <h2>Примечания к дизайну</h2>
    <p>Следует признать, что многие аспекты HTML кажутся на первый взгляд бессмысленными и непоследовательными.</p>
    <p>HTML, поддерживающие его API-интерфейсы DOM, а также многие поддерживающие его технологии разрабатывались в течение нескольких десятилетий широким кругом людей с разными приоритетами, которые во многих случаях не знали о существовании друг друга.</p>
    <p>Таким образом, функции возникли из многих источников и не всегда разрабатывались особенно последовательным образом. Кроме того, из-за уникальных характеристик Интернета ошибки реализации часто становятся де-факто, а теперь и де-юре стандартами, так как контент часто непреднамеренно записывается таким образом, который полагается на них, прежде чем они могут быть исправлены.</p>
    <p>Несмотря на все это, были предприняты усилия, чтобы придерживаться определенных целей дизайна. Они описаны в следующих нескольких подразделах.</p>
    <hr>

    <h3>Соответствие другим спецификациям</h3>
    <p>Эта спецификация взаимодействует с множеством других спецификаций и полагается на них. К сожалению, при определенных обстоятельствах противоречивые потребности привели к тому, что эта спецификация нарушила требования этих других спецификаций. Каждый раз, когда это происходило, каждое нарушение отмечалось как &quot;умышленное нарушение&quot;, и указывалась причина нарушения.</p>
