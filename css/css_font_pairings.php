    <h2>Правила сочетания шрифтов</h2>
    <p>Вот несколько основных правил для создания отличных пар шрифтов:</p>

    <h3>1. Комплимент</h3>
    <p>Всегда безопасно находить сочетания шрифтов, которые дополняют друг друга.</p>
    <p>Хорошая комбинация шрифтов должна гармонировать, не быть слишком похожей или слишком разной.</p>

    <h3>2. Использование суперсемейства шрифтов</h3>
    <p>Суперсемейство шрифтов - это набор шрифтов, предназначенный для совместной работы. Таким образом, использование разных шрифтов в одном суперсемействе безопасно.</p>
    <p>Например, суперсемейство Lucida содержит следующие шрифты: Lucida Sans, Lucida Serif, Lucida Typewriter Sans, Lucida Typewriter Serif и Lucida Math.</p>

    <h3>3. Контраст - это король</h3>
    <p>Два слишком похожих шрифта часто конфликтуют. Тем не менее, контрасты, выполненные правильно, раскрывают все лучшее в каждом шрифте.</p>
    <p>Пример. Комбинация шрифтов с засечками и без засечек - хорошо известная комбинация.</p>
    <p>Сильное суперсемейство включает варианты одного и того же шрифта с засечками и без засечек (например, Lucida и Lucida Sans).</p>

    <h3>4. Выберите только одного босса</h3>
    <p>Один шрифт должен быть боссом. Это устанавливает иерархию шрифтов на вашей странице. Этого можно добиться, варьируя размер, вес и цвет.</p>

    <div class="w3-example">
        <h3>Пример</h3>
        <p>Без сомнения, &quot;Georgia&quot; здесь босс:</p>
