    <h2>Что такое jQuery?</h2>
    <p><strong>jQuery</strong> - это сверхлёгкая JavaScript библиотека - &quot;пиши меньше, делай больше&quot;.</p>
    <p>Цель <strong>jQuery</strong> - упростить использование JavaScript на вашем веб-сайте.</p>
    <p><strong>jQuery</strong> берет на себя множество общих задач, для выполнения которых требуется много строк кода JavaScript, и превращает их в методы, которые можно вызывать с помощью одной строки кода.</p>
    <p><strong>jQuery</strong> также упрощает многие сложные вещи из JavaScript, такие как вызовы AJAX и манипуляции с DOM.</p>
    <p>Библиотека jQuery содержит следующие функции:</p>
    <ul>
      <li>HTML/DOM манипуляция</li>
      <li>CSS манипуляция</li>
      <li>HTML методы событий</li>
      <li>Эффекты и анимации</li>
      <li>AJAX</li>
      <li>Утилиты</li>
    </ul>
    <p><b>Совет:</b> Кроме того, в jQuery есть плагины практически для любых задач.</p>
