    <h2>Что такое SVG?</h2>
    <ul>
        <li>SVG - это масштабируемая векторная графика</li>
        <li>SVG используется для определения векторной графики для Интернета</li>
        <li>SVG определяет графику в формате XML</li>
        <li>Каждый элемент и каждый атрибут в файлах SVG можно анимировать</li>
        <li>SVG - это рекомендация W3C</li>
        <li>SVG интегрируется с другими стандартами W3C, такими как DOM и XSL</li>
    </ul>
    <hr>

    <h2>SVG - это рекомендация W3C</h2>
    <p>SVG 1.0 стал Рекомендацией W3C 4 сентября 2001 г.</p>
    <p>SVG 1.1 стал Рекомендацией W3C 14 января 2003 г.</p>
    <p>SVG 1.1 (второе издание) стал рекомендацией W3C 16 августа 2011 г.</p>
    <hr>

    <h2>SVG Преимущества</h2>
    <p>Преимущества использования SVG перед другими форматами изображений (такими как JPEG и GIF):</p>
    <ul>
        <li>Изображения SVG можно создавать и редактировать с помощью любого текстового редактора</li>
        <li>Изображения SVG можно искать, индексировать, создавать сценарии и сжимать</li>
        <li>Изображения SVG можно масштабировать</li>
        <li>Изображения SVG можно печатать с высоким качеством при любом разрешении</li>
        <li>Графика SVG НЕ теряет качества при увеличении или изменении размера</li>
        <li>SVG - это открытый стандарт</li>
        <li>Файлы SVG - это чистый XML</li>
    </ul>
    <hr>

    <h2>Создание SVG Изображений</h2>
