    <h2>16 миллионов различных цветов</h2>
    <p>Сочетание красного, зеленого и синего значений от 0 до 255 дает в целом более 16000000 различных цветов (256 x 256 x 256).</p>
    <p>Большинство современных мониторов способны отображать не менее 16384 различных цветов.</p>
    <hr>

    <h2>Что такое веб-безопасные цвета?</h2>
    <p>Много лет назад, когда компьютеры поддерживали максимум 256 различных цветов, появился список из 216 "цветов, безопасных для веб" было предложено как веб-стандарт (с резервированием 40 фиксированных системных цветов).</p>
    <p>Сейчас это не важно, поскольку большинство компьютеров могут отображать миллионы разных цветов.</p>
    <p>Эта цветовая палитра цветов с 216 шестнадцатеричных значений в веб-браузере была создана для того, чтобы все компьютеры правильно отражали цвета при запуске 256 цветовой палитры:</p>

