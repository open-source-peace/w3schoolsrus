    <h2>Windows-1252</h2>
    <p>Windows-1252 был первым набором символов по умолчанию в Microsoft Windows.</p>
    <p>Это был самый популярный набор символов в Windows с 1985 по 1990 годы.</p>

    <hr>
    <h2>ANSI</h2>
    <p>Исторически термин "ANSI Code Pages" использовался в Windows для обозначения наборов символов, не относящихся к DOS.</p>
    <p>Предполагалось, что эти наборы символов будут соответствовать стандартам ANSI, таким как ISO-8859-1.</p>
    <p>Несмотря на то, что Windows-1252 практически идентична ISO-8859-1, она никогда не была стандартом ANSI или ISO.</p>
    <hr>

    <h2>Windows-1252 и ASCII</h2>
    <p>Первая часть Windows-1252 (номера символов от 0 до 127) - это оригинальный набор символов ASCII. Он содержит цифры, прописные и строчные буквы английского алфавита и некоторые специальные символы.</p>
    <p>Для более детального ознакомления, пожалуйста, изучите наш <a href="ref_html_ascii.php">Полный справочник ASCII</a>.</p>
