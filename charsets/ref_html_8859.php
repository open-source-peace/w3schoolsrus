    <h2>ISO-8859-1 и Windows-1252</h2>
    <p>ISO-8859-1 очень похож на Windows-1252.</p>
    <p><strong>В ISO-8859-1 символы от 128 до 159 не определены.</strong></p>
    <p>В Windows-1252 символы от 128 до 159 используются для некоторых полезных символов.</p>
    <p>Для более подробного ознакомления, пожалуйста, изучите наш  <a href="ref_html_ansi.php">Полный справочник ANSI (Windows-1252)</a>.</p>
    <p>Поскольку многие веб-сайты декларируют ISO-8859-1 и используют значения от 128 до 159, как если бы они использовали Windows-1252, большинство браузеров будут отображать эти символы из набора символов Windows-1252 вместо ничего.</p>

