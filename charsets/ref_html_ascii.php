    <h2>Набор символов ASCII</h2>
    <p>ASCII расшифровывается как "American Standard Code for Information Interchange" (Американский стандартный код для обмена информацией).</p>
    <p>Он был разработан в начале 1960-х годов как стандартный набор символов для компьютеров и электронных устройств.</p>
    <p>ASCII - это 7-битный набор символов, содержащий 128 символов.</p>
    <p>Он содержит цифры от 0 до 9, заглавные и строчные буквы английского алфавита от A до Z и некоторые специальные символы.</p>
    <p>Наборы символов, используемые на современных компьютерах в HTML и в Интернете, основаны на ASCII.</p>
    <p>В следующих таблицах перечислены 128 символов ASCII и их эквивалентное число.</p>
