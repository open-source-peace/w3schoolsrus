    <h2>Bootstrap 3 vs. Bootstrap 4</h2>
    <p><strong>Bootstrap 4</strong> - новейшая версия Bootstrap; с новыми компонентами, более быстрой таблицей стилей и большей отзывчивостью (адаптивностью).</p>
    <p><strong>Bootstrap 4</strong> поддерживает последние стабильные выпуски всех основных браузеров и платформ. Тем не менее, Internet Explorer 9 и ниже не поддерживается.</p>
    <div class="w3-panel w3-note">
        <p><strong>Если вам требуется поддержка IE8-9, используйте <a href="../bootstrap/index.php">Bootstrap 3.</a></strong> Это самая стабильная версия Bootstrap, и она все еще поддерживается командой для критических исправлений и изменений в документации. Тем не менее, новые функции не будут добавлены к нему.</p>
        <p><strong>Поддержка удаленных иконок</strong>: Bootstrap 4 не поддерживает BS3 Glyphicons. Используйте <a href="../icons/index.php">Font-Awesome или другие библиотеки иконок вместо этого</a>.</p>
    </div>
