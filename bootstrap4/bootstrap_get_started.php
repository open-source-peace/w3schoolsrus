    <h2>Зачем использовать Bootstrap?</h2>
    <p>Преимущества Bootstrap:</p>
    <ul>
        <li><strong>Простота в использовании:</strong> любой, кто имеет базовые знания HTML и CSS, может начать использовать Bootstrap.</li>
        <li><strong>Адаптивные функции:</strong> адаптивный CSS Bootstrap подстраивается под телефоны, планшеты и настольные компьютеры</li>
        <li><strong>Mobile-first подход (первые - мобильные устройства):</strong> В Bootstrap стили для мобильных устройств являются частью базовой структуры</li>
        <li><strong>Браузерная совместимость:</strong> Bootstrap 4 совместим со всеми современными браузерами (Chrome, Firefox, Internet Explorer 10+, Edge, Safari и Opera).</li>
    </ul>
