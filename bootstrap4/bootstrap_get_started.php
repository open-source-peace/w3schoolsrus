    <h2>Создать первую веб-страницу с Bootstrap 4</h2>
    <p><strong>1. Добавьте тип документа HTML5</strong></p>
    <p>Bootstrap 4 использует элементы HTML и свойства CSS, для которых требуется тип документа HTML5.</p>
    <p>Всегда включайте тип документа HTML5 в начале страницы вместе с атрибутом <code class='w3-codespan'>lang</code> и правильным набором символов (стандартно - utf-8):</p>
    <div class="w3-example">
        <div class="w3-code notranslate htmlHigh">
            &lt;!DOCTYPE html&gt;<br>&lt;html lang=&quot;en&quot;&gt;<br>&nbsp; &lt;head&gt;<br>&nbsp;&nbsp;&nbsp; &lt;meta charset=&quot;utf-8&quot;&gt; <br>&nbsp; &lt;/head&gt;<br>&lt;/html&gt;</div>
    </div>

    <p><strong>2. Bootstrap 4 является mobile-first</strong></p>
    <p>Bootstrap 4 разработан, чтобы реагировать на мобильные устройства. Mobile-first стили являются частью основной структуры.</p>
    <p>Чтобы обеспечить правильный рендеринг и масштабирование касанием, добавьте следующий тег <code class="w3-codespan">&lt;meta&gt;</code> внутри элемента <code class="w3-codespan">&lt;head&gt;</code>:</p>
    <div class="w3-example">
        <div class="w3-code notranslate htmlHigh">
            &lt;meta name=&quot;viewport&quot; content=&quot;width=device-width, initial-scale=1&quot;&gt;</div>
    </div>

    <p>Часть <code class="w3-codespan">width=device-width</code> устанавливает ширину страницы в соответствии с шириной экрана устройства (которая зависит от устройства).</p>
    <p>Часть <code class="w3-codespan">initial-scale=1</code> устанавливает начальный уровень масштабирования при первой загрузке страницы браузером.</p>
    <p><strong>3. Контейнеры</strong></p>
    <p>Bootstrap 4 также требует содержащего элемента для переноса содержимого сайта.</p>
    <p>Есть два контейнерных класса на выбор:</p>
    <ol>
        <li>Класс <code class="w3-codespan">.container</code> обеспечивает адаптивный <strong>контейнер фиксированной ширины</strong></li>
        <li>Класс <code class="w3-codespan">.container-fluid</code> обеспечивает <strong>контейнер на всю ширину</strong>, охватывающий всю ширину области просмотра</li>
    </ol>

