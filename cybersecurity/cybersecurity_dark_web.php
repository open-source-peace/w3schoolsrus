    <h2>Dark Web - Темная паутина</h2>
    <p><strong>Dark Web</strong> (также называемая <strong>Dark Net</strong>) - это сеть в Интернете, доступная только с использованием определенного программного обеспечения и протоколов.</p>
    <p>У Dark Web много названий, например, <em>Tor Network</em> или <em>Onion Router</em>.</p>
    <p>Любой может получить доступ к Dark Web, просто загрузив для него программное обеспечение. Популярным и очень часто используемым браузером является
        <a href="https://ru.wikipedia.org/wiki/Tor" target="_blank" rel="nofollow" title="Узнать про Tor браузер на Wikipedia">Tor Browser</a> от <a href="https://www.torproject.org/" target="_blank" rel="nofollow" title="Перейти на официальный сайт Tor Project">Tor Project</a>.</p>
    <p>Браузер Tor похоже на любой другой браузер, такой как Google Chrome, Mozilla Firefox, Opera или Microsoft Edge, за исключением того, что он также может получать доступ к специальным адресам веб-сайтов, которые заканчиваются на <i>.onion</i> вместо <i>.com</i> и т.д.</p>
    <p>Любой трафик, отправляемый через Tor Browser, автоматически анонимизируется и шифруется через множество различных хостов. Браузер также имеет встроенную защиту для многих видов функций отслеживания и деанонимизации.</p>
