    <h2>Уровень 6 - Уровень представления</h2>
    <p>Обычно невидимый уровень, но отвечает за адаптацию, преобразование и перевод данных. Это сделано для того, чтобы приложение и нижележащие слои понимали друг друга.</p>

    <ul>
        <li>Схемы кодирования, используемые для представления текста и данных, например ASCII (американский стандартный код для обмена информацией) и UTF (формат преобразования Unicode).</li>
        <li>Шифрование для сервисов, например SSL (&quot;Secure Sockets Layer&quot; - &quot;Уровень защищенных гнезд&quot;) и TLS (&quot;Transport Security Layer&quot; - &quot;Уровень безопасности транспортировки&quot;)</li>
        <li>Сжатие, например GZip, используемый во многих реализациях HTTP.</li>
    </ul>
    <hr>

    <h2>Уровень 5 - Уровень сессии</h2>
    <p>Менее обсуждаемый уровень модели OSI. Этот уровень отвечает за обработку соединений между приложением и нижележащими уровнями. Он включает в себя установление, поддержание и завершение соединений, иначе называемых сессиями.</p>
    <p>Общие протоколы, которые хорошо представляют уровень сессии:</p>
    <ul>
        <li>SOCKS - Протокол отправки пакетов через прокси-сервер.</li>
        <li>NetBIOS - Старый протокол Windows для установления сеансов и разрешения имен.</li>
        <li>SIP (&quot;Session Initiation Protocol&quot; - &quot;Протокол инициирования сеанса&quot;) - Для участия в VOIP (&quot;Voice Over IP&quot; - &quot;Голос по IP&quot;) связи.</li>
    </ul>
    <hr>

    <h2>Уровень 4 - Транспортировка</h2>
    <p>Уровень, который позволяет приложениям быть представленными в сети.</p>
    <p>Некоторые хорошо известные приложения на этом уровне:</p>
    <ul>
        <li>TCP (&quot;Transmission Control Protocol&quot; - &quot;Протокол управления передачей&quot;) - Используется для многих приложений, обеспечивая стабильность, контроль того, сколько данных может быть отправлено в любой момент времени, надежность и многое другое.</li>
        <li>UDP (&quot;User Datagram Protocol&quot; - &quot;Протокол пользовательских датаграмм&quot;) - Легкое и быстрое использование протокола для многих сервисов.</li>
        <li>QUIC (&quot;Quick UDP Internet Connections&quot; - &quot;Быстрое подключение к Интернету по протоколу UDP&quot;) - Протокол, разработанный для более быстрых соединений и идущий рука об руку с версией 2 протокола HTTP.</li>
    </ul>
    <hr>

    <h2>Уровень 3 - Сеть</h2>
    <p>Уровень, отвечающий за маршрутизацию пакетов между сетями через маршрутизаторы.</p>
    <p>На этом уровне находятся следующие протоколы:</p>
    <ul>
        <li>IP (&quot;Internet Protocol&quot; - &quot;Интернет протокол&quot;) - Используется каждый день при доступе в Интернет. Поставляется в двух версиях: IP версии 4 и 6.</li>
        <li>ICMP (&quot;Internet Control Message Protocol&quot; - &quot;Протокол управляющих сообщений Интернета&quot;) - Используется сетевыми устройствами и сетевыми операторами для диагностики сетевых подключений или для устройств, чтобы отправлять сообщения об ошибках и реагировать на них, и т.д.</li>
        <li>IPSec (&quot;Internet Protocol Security&quot; - &quot;Безопасность интернет-протокола&quot;) - Разрешает зашифрованные и безопасные соединения между двумя сетевыми устройствами.</li>
    </ul>
    <hr>

    <h2>Уровень 2 - Ссылка (канал)</h2>
    <p>Ссылочные (канальные) сети, как следует из названия, состоят из протоколов, предназначенных для отправки пакетов через фактические ссылки, то есть физические соединения, к которым подключены сетевые узлы. Проще говоря, канальный уровень отвечает за перемещение данных с физического на логический (на сетевой уровень).</p>
    <p>Протоколы на этом уровне включают:</p>
    <ul>
        <li>Ethernet - важный протокол, используемый большинством операционных систем при подключении к сетям с помощью физического кабеля.</li>
        <li>Wi-Fi (&quot;Wireless Fidelity&quot; - &quot;Беспроводное соединение&quot;) - Для доступа к сетям через радиосигналы. Он использует семейство протоколов, называемых IEEE 802.11.xx</li>
        <li>NDP (&quot;Neighbor Discovery Protocol&quot; - &quot;Протокол обнаружения соседей&quot;) - IP версии 6 (IPv6) использует этот протокол на канальном уровне для сбора информации, необходимой для связи через IPv6.</li>
    </ul>
    <hr>

    <h2>Уровень 1 - Физический</h2>
    <p>Физический уровень представляет собой сигнализацию, которая позволяет битам и байтам передаваться между физической средой. Он может передаваться по радио или сигналам по кабелю, с использованием электрических сигналов или света, например, по оптоволокну.</p>
    <p>Примеры протоколов физического уровня включают:</p>
    <ul>
        <li>CAN Bus (&quot;Controller Area Network&quot; - &quot;Сеть контроллеров&quot;) - Используется в микроконтроллерах и других устройствах для связи с другими подобными устройствами, без использования компьютера. Часто используется в ICS ("Industrial Control Systems").</li>
        <li>Ethernet Physical Layer (Физический уровень Ethernet) - Используется Ethernet на физическом уровне для отправки сигналов со скоростью до многих гигабит трафика в секунду.</li>
        <li>Bluetooth Physical Layer (Физический уровень Bluetooth) - Bluetooth также имеет свои собственные спецификации того, как должны отправляться и приниматься радиосигналы.</li>
    </ul>
