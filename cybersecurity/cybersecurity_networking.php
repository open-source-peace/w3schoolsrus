    <h2>Уровень 7 - Уровень приложения</h2>
    <p>Здесь кроется бизнес-логика и функциональность приложения. Это то, что пользователи используют для взаимодействия со службами в сети. Большинство разработчиков создают приложения на уровне приложений.</p>
    <p>Большинство приложений, которые вы используете, находятся на уровне приложений, при этом сложность других уровней скрыта.</p>
    <p>Примеры приложений уровня 7:</p>
    <ul>
        <li>HTTP (&quot;Hyper Text Transport Protocol&quot; - &quot;Протокол передачи гипертекста&quot;) - Позволяет нам получать доступ к веб-приложениям</li>
        <li>FTP (&quot;File Transfer Protocol&quot; - &quot;Протокол передачи файлов&quot;) - Позволяет пользователям передавать файлы</li>
        <li>SNMP (&quot;Simple Network Management Protocol&quot; - &quot;Простой протокол управления сетью&quot;) - Протокол для чтения и обновления конфигураций сетевых устройств</li>
    </ul>
    <p>Эти протоколы используются во многих приложениях, таких как Google Chrome, Microsoft Skype и FileZilla.</p>
    <div class="w3-panel w3-note">
        <p>Вы получаете доступ к этому классу через уровень 7!</p>
    </div>
