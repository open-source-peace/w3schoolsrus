    <h2>Киберпреступники</h2>
    <p>Кто такие киберпреступники? Это может быть кто угодно, но давайте рассмотрим нечто общее в них, что мы постоянно видим и слышим в СМИ:</p>
    <ul>
        <li>Спонсируемые государством (спецслужбами) группы, нацеленные на компании, организации в других странах</li>
        <li>Взлом групп, нацеленных на компании, чтобы заставить их платить выкуп (кибертероризм)</li>
        <li>Частные лица в домашних условиях занимаются незаконным взломом как акаунтов других частных лиц, так и компьютерных систем в различных компаниях, организациях, учреждениях (как правило - на заказ)</li>
    </ul>
