    <h2>Как поймать киберпреступников?</h2>
    <p>Киберпреступники долго могут оставаться анонимными, используя тривиальные средства, и, как правило, им это удаётся. Когда же киберпреступники попадают в ловушку, то часто это происходит из-за ошибок самих киберпреступников, например, в результате самоуверенности, самоуспокоенности, ощущения вседозволенности и безнаказанности. У правоохранительных органов также есть большой потенциал использования пойманных киберпреступников для обнаружения киберпреступников в данной сфере.</p>
