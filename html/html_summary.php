    <h2>Изучение CSS</h2>
    <p><strong>CSS</strong> позволяет стилизировать HTML-страницы.</p>
    <p><strong>CSS</strong> предоставляет полный контроль над макетом, не изменяя содержания документа.</p>
    <p>Чтобы узнать больше о <strong>CSS</strong>, посетите <a href='../css/index.php'>CSS учебник</a> на нашем сайте <strong>W3Schools на русском</strong>.</p>
    <hr>

    <h2>Изучение JavaScript</h2>
    <p><strong>JavaScript</strong> делает сайт более динамичным. Динамичный веб-сайт может реагировать на события и позволяет взаимодействовать с пользователем.</p>
    <p><strong>JavaScript</strong> является на сегодняшний день самым популярным скриптовым языком в Интернете (на стороне клиента), и он работает во всех основных браузерах.</p>
    <p>Если вы хотите узнать больше про <strong>JavaScript</strong>, пожалуйста, посетите <a href='../js/index.php'>JavaScript учебник</a> на нашем сайте <strong>W3Schools на русском</strong>.</p>
