    <h2>HTML Резюме</h2>
    <p>Этот учебник научил вас использовать <strong>HTML</strong> для создания собственного веб-сайта.</p>
    <p><strong>HTML</strong> является универсальным языком разметки для Web. <strong>HTML</strong> позволяет форматировать текст, добавлять графику, создавать ссылки, формы ввода, фреймы, таблицы и др., а также сохранять их в текстовом файле, который может читать и отображать любой веб-браузер.</p>
    <p>Для получения более подробной и дополнительной информации об <strong>HTML</strong>, пожалуйста, обратитесь к <a href='html_examples.php'>HTML примерам</a> и <a href='../tags/index.php'>HTML справочнику</a> на нашем сайте <strong>W3Schools на русском</strong>.</p>
