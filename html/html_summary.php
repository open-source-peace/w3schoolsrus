    <h2>Публикация веб-сайта</h2>
    <p>Чтобы сделать свой веб-сайт доступным для каждого в мире пользователя Интернет, его необходимо опубликовать в сети.</p>
    <p>Для этого у вас есть два варианта:</p>
    <ul>
      <li>Использование поставщика услуг Интернета (сервис-провайдера);</li>
      <li>Собственный хостинг веб-сайта.</li>
    </ul>
    <hr>

    <h2>Использование услуг сервис-провайдера Интернет</h2>
    <p>Интернет-провайдер (ISP - Internet Service Provider) - это компания, которая предоставляет услуги доступа к Интернету и использования Интернета.</p>
    <p>Интернет-услуги, которые обычно предоставляются Интернет-провайдерами, включают доступ к Интернету, Интернет-транзит, регистрацию доменного имени, веб-хостинг, услугу Usenet и колокацию.</p>
    <p>Использование почтавщика услуг Интернета (ISP - Интернет сервис-провайдера) является самым распространённым вариантом.</p>

    <h3>Преимущества:</h3>
    <ul>
      <li><strong>Скорость соединения</strong> - Провайдеры имеют очень скоростное соединение с Интернетом;</li>
      <li><strong>Мощное оборудование</strong> - Провайдеры имеют мощные веб-серверы, которые могут совместно использовать несколько клиентов. Можно также ожидать эффективной сбалансированной нагрузки и необходимых серверов резервного копирования;</li>
      <li><strong>Безопасность и стабильность</strong> - Провайдеры - это специалисты веб-хостинга. Вы получите намного больше свободного времени, самые новые исправления программного обеспечения и лучшую защиту от вирусов.</li>
    </ul>

    <h3>Что необходимо учитывать?</h3>

    <ul>
      <li><strong>24-часовая поддержка</strong> - Провайдер должен предложить 24-часовую поддержку. Бесплатный телефон также может быть жизненно важен;</li>
      <li><strong>Ежедневный бекап (резервное копирование)</strong> - Провайдер должен выполнять ежедневную процедуру резервного копирования;</li>
      <li><strong>Объём трафика</strong> - Проверьте ограничения объёма трафика поставщика услуг Интернета (не прекращайте платить деньги за неожиданно высокий трафик);</li>
      <li><strong>Ограничения пропускной способности или содержания</strong> - Проверьте пропускную способность провайдера и ограничения содержания (можно ли публиковать фотографии, видео или аудио?);</li>
      <li><strong>Возможности электронной почты</strong> - Убедитесь, что провайдер поддерживает необходимые возможности электронной почты;</li>
      <li><strong>Доступ к базе данных</strong> - Убедитесь, что провайдер поддерживает необходимый доступ к базе данных.</li>
    </ul>
    <hr>

    <h2>Хостинг собственного сайта на своём сервере</h2>
    <p>Хостинг собственного веб-сайта на вашем собственном сервере также является возможным.</p>

    <h3>Что необходимо учитывать?</h3>

    <ul>
      <li><strong>Затраты на оборудование</strong> - Чтобы запустить &quot;реальный&quot; веб-сайт, вы должны приобрести мощное серверное оборудование (обычный домашний ПК не будет выполнять необходимую работу). Вам также понадобится постоянное (24/7) высокоскоростное Интернет-соединение;</li>
      <li><strong>Затраты на программное обеспечение</strong> - Серверные лицензии часто превышают стоимость клиентских лицензий. Серверные лицензии также могут иметь ограничения на количество пользователей;</li>
      <li><strong>Затраты на оплату труда</strong> - Не ожидайте низких трудовых затрат. Вы должны установить собственное аппаратное и программное обеспечение. Вы также будете иметь дело с ошибками и вирусами, и должны содержать ваш сервер постоянно работающим.</li>
    </ul>
