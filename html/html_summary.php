    <h2>Теперь вы знаете HTML, что дальше?</h2>
    <p>Что необходимо изучать после изучения <strong>HTML</strong>?</p>
    <p>Как правило, после изучения <strong>HTML</strong> изучают также такие технологии, как <strong>CSS</strong> и <strong>JavaScript</strong> (для фронтенд-разработчиков).</p>
