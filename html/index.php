<h2>Что такое HTML? Как создают веб-сайты?</h2>
<p><strong>HTML</strong> (от англ. <em>HyperText Markup Language — "язык гипертекстовой разметки"</em>) — стандартизированный язык разметки документов во Всемирной паутине. Большинство веб-страниц имеют содержание разметки на языке HTML (или XHTML). Язык <strong>HTML</strong> интерпретируется браузерами; полученный в результате интерпретации форматированный текст отображается на экране монитора компьютера или мобильного устройства.</p>
<p>Язык <strong>HTML</strong> до 5-й версии определялся как приложение SGML (стандартного обобщённого языка разметки по стандарту ISO 8879). Спецификации <strong>HTML5</strong> формулируются в терминах DOM (объектной модели документа).</p>
<p>Язык <strong>XHTML</strong> является более строгим вариантом <strong>HTML</strong>, он следует синтаксису XML и является приложением языка XML в области разметки гипертекста.</p>
<p>Во всемирной паутине HTML-страницы, как правило, передаются браузерам от сервера по протоколам <em>HTTP</em> или <em>HTTPS</em>, в виде простого текста или с использованием шифрования.</p>
<p class="w3-right-align">Информация из <a href="https://ru.wikipedia.org/wiki/HTML" target="_blank" rel="nofollow">Википедии</a></p>
