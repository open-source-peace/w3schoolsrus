    <h2>HTML5 - Синтаксис новых атрибутов</h2>
    <p>HTML5 допускает четыре разных синтаксиса для атрибутов.</p>
    <p>Этот пример демонстрирует различные синтаксисы, используемые в теге <code class='w3-codespan'>&lt;input&gt;</code>:</p>
    <table class='w3-table-all notranslate'>
      <tr>
        <th style='width:15%'>Type</th>
        <th>Пример</th>
      </tr>
      <tr>
        <td>Пустой</td>
        <td>&lt;input type=&quot;text&quot; value=&quot;John&quot; <strong>disabled</strong>&gt;</td>
      </tr>
      <tr>
        <td>Без кавычек</td>
        <td>&lt;input type=&quot;text&quot; <strong>value=John</strong>&gt;</td>
      </tr>
      <tr>
        <td>Двойные кавычки</td>
        <td>&lt;input type=&quot;text&quot; <strong>value=&quot;John Doe&quot;</strong>&gt;</td>
      </tr>
      <tr>
        <td>Одинарные кавычки</td>
        <td>&lt;input type=&quot;text&quot; <strong>value='John Doe'</strong>&gt;</td>
      </tr>
    </table>
    <p>В HTML5 могут использоваться все четыре синтаксиса, в зависимости от того, что требуется для атрибута.</p>
