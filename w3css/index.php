    <h2>Как использовать W3.CSS?</h2>
    <p>Чтобы использовать W3.CSS на своем веб-сайте, просто добавьте ссылку на &quot;w3.css&quot; со своих веб-страниц:</p>
    <div class="w3-example">
        <h3>Пример</h3>
        <div class="w3-code notranslate htmlHigh">
            &lt;link rel="stylesheet" href="https://www.w3schools.com/w3css/4/w3.css"&gt;
        </div>
        <a class="w3-btn w3-margin-bottom" href="../w3csstryit/tryw3css_default_howto.html" target="_blank">Попробуйте сами &raquo;</a>
    </div>
    <p>Или загрузите w3.css из <a href="w3css_downloads.php">w3css_downloads</a> и добавьте ссылку на w3.css:</p>
    <div class="w3-example">
        <h3>Пример</h3>
        <div class="w3-code notranslate htmlHigh">
            &lt;link rel="stylesheet" href="w3.css"&gt;
        </div>
    </div>
    <hr>

    <h2>Альтернативная ссылка на W3.CSS</h2>
    <p>Также вы можете воспользоваться альтернативной ссылкой с нашего сайта &quot;W3Schools на русском&quot; для подключения фреймворка W3.CSS на своем веб-сайте, просто добавив ссылку на своих веб-страницах:</p>
    <div class="w3-example">
        <h3>Пример</h3>
        <div class="w3-code notranslate htmlHigh">
            &lt;link rel="stylesheet" href="https://w3schoolsrus.github.io/lib/w3.css"&gt;
        </div>
    </div>
    <p>Также вы можете разместить файл <em>w3.css</em> на своём сайте и добавить ссылку на файл <em>w3.css</em> на каждой веб-странице между тегами <code class="w3-codespan">&lt;head&gt;</code> и <code class="w3-codespan">&lt;/head&gt;</code>, где используется данный фреймворк:</p>
    <div class="w3-example">
        <h3>Пример</h3>
        <div class="w3-code notranslate htmlHigh">
            &lt;link rel="stylesheet" href="w3.css"&gt;
