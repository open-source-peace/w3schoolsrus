    <h2>W3.CSS - это CSS фреймворк</h2>
    <p><strong>W3.CSS</strong> - это современный CSS-фреймворк с поддержкой отображения по умолчанию для десктопа, планшета и мобильного устройства.</p>
    <p><strong>W3.CSS</strong> меньше по объёму и работает быстрее, чем аналогичные CSS-фреймворки.</p>
    <p><strong>W3.CSS</strong> был разработан, чтобы быть высококачественной альтернативой Bootstrap.</p>
    <p><strong>W3.CSS</strong> был разработан, чтобы быть меньше, быстрее, чтобы его можно было легче освоить и легче использовать.</p>
    <p><strong>W3.CSS</strong> также был разработан, чтобы быть независимым от jQuery или любой другой библиотеки JavaScript.</p>

