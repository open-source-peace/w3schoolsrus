    <h3>Подход P-значения</h3>
    <p>Для подхода P-значения нам нужно найти <strong>P-значение</strong> тестовой статистики (TS).</p>
    <p>Если P-значение <b>меньше</b> уровня значимости (\(\alpha\)), мы <b>отклоняем</b> нулевую гипотезу (\(H_{0}\)).</p>
    <p>Статистика теста оказалась \( \approx \underline{2.889} \)</p>
    <p>Для теста доли населения статистика теста представляет собой T-значение из <a href="students_t_distribution.php">стьюдент t-распределения</a>.</p>
    <p>Поскольку это <b>правосторонний</b> тест, нам нужно найти P-значение для T-значения <b>больше</b> чем 2,889.</p>
    <p>Стьюдент T-распределение корректируется в соответствии со степенями свободы (df), которые являются размером выборки \((30) - 1 = \underline{29}\)</p>
    <p>Мы можем найти P-значение с помощью <a href="statistics_t_table.php">T-таблицы</a> или с помощью функции языка программирования:</p>
    <div class="w3-example">
