    <h2>5. Заключение</h2>
    <p>Существует два основных подхода к заключению проверки гипотезы:</p>
    <ul>
        <li>Подход <strong>критического значения</strong> сравнивает статистику теста с критическим значением уровня значимости</li>
        <li>Подход <strong>P-значения</strong> сравнивает P-значение тестовой статистики и уровень значимости</li>
    </ul>
    <div class="w3-panel w3-note">
        <p><b>Примечание.</b> Эти два подхода различаются только тем, как они представляют заключение.</p>
    </div>
    <h3>Подход критических значений</h3>
    <p>Для подхода критического значения нам нужно найти <strong>критическое значение</strong> (CV) уровня значимости (\(\alpha\)).</p>
    <p>Для теста на среднее значение для генеральной совокупности критическое значение (CV) - это <strong>T-значение</strong> из <a href="students_t_distribution.php">стьюдент t-распределения</a>.</p>
    <p>Это критическое T-значение (CV) определяет <strong>область отклонения</strong> для теста.</p>
    <p>Область отклонения - это область вероятности в хвостах стандартного нормального распределения.</p>
    <p>Поскольку утверждается, что среднее значение генеральной совокупности <b>больше</b> чем 55, область отклонения находится в правом хвосте:</p>
    <p><img src="../svg/img_critical_value_right_prop1.svg" alt="Стандартное нормальное распределение с правой хвостовой областью (область отклонения), обозначенной греческим символом alpha"></p>
    <p>Размер области отклонения определяется уровнем значимости (\(\alpha\)).
    <p>Стьюдент t-распределение скорректировано с учетом неопределенности по меньшим выборкам.</p>
    <p>Эта корректировка называется степенями свободы (df), то есть размером выборки \((n) - 1\)</p>
    <p>В этом случае степени свободы (df) равны: \(30 - 1 = \underline{29} \)</p>
    <p>Выбрав уровень значимости (\(\alpha\)) 0,01 или 1&#37;, мы можем найти критическое T-значение из <a href="statistics_t_table.php">T-таблицы</a>, или с помощью функции языка программирования:</p>
    <div class="w3-example">
        <h3>Пример</h3>
        <p>В Python используйте функцию библиотеки Scipy Stats <code class="w3-codespan">t.ppf()</code> найдите T-значение для \(\alpha\) = 0,01 при 29 степенях свободы (df).</p>
        <div class="w3-code notranslate pythonHigh">
            import scipy.stats as stats
            <br>
            print(stats.t.ppf(1-0.01, 29))
        </div>
        <a target="_blank" class="w3-btn w3-margin-bottom" href="../pythontryit/demo_stat_python_t_score2.html">Попробуйте сами &raquo;</a>
    </div>
    <div class="w3-example">
        <h3>Пример</h3>
        <p>В R используйте встроенную функцию <code class="w3-codespan">qt()</code> чтобы найти t-значение для \(\alpha\) = 0,01 при 29 степенях свободы (df).</p>
        <div class="w3-code notranslate pythonHigh">
            qt(1-0.01, 29)
        </div>
        <a target="_blank" class="w3-btn w3-margin-bottom" href="../rtryit/demo_stat_r_t_score2.html">Попробуйте сами &raquo;</a>
    </div>
    <p>Используя любой метод, мы можем найти, что критическое T-значение равно \(\approx \underline{2.462}\)</p>
    <p>Для <b>правостороннего</b> теста необходимо проверить, не превышает ли тестовая статистика (TS) критическое значение (CV).</p>
    <p>Если тестовая статистика больше критического значения, то тестовая статистика находится в <strong>области отклонения</strong>.</p>
    <p>Когда статистика теста находится в области отклонения, мы <b>отклоняем</b> нулевую гипотезу (\(H_{0}\)).</p>
    <p>Здесь тестовая статистика (TS) была \(\approx \underline{2.889}\), а критическое значение было \(\approx \underline{2.462}\)</p>
    <p>Вот иллюстрация этого теста в виде графика:</p>
    <p><img src="../svg/img_critical_value_right_mean2.svg" alt="Т-распределение Стьюдента с областью правого хвоста (область отклонения), равной 0,01, критическим значением 2,462 и тестовой статистикой 2,889"></p>
    <p>Поскольку статистика теста была <b>больше</b> критического значения, мы <b>отклоняем</b> нулевую гипотезу.</p>
    <p>Это означает, что выборки данных подтверждают альтернативную гипотезу.</p>
