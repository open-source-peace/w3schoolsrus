    <h2>3. Определение уровня значимости</h2>
    <p>Уровень значимости (\(\alpha\)) - это <strong>неопределённость</strong>, которую мы принимаем при отклонении нулевой гипотезы в проверке гипотез.</p>
    <p>Уровень значимости - это процентная вероятность случайного ошибочного вывода.</p>
    <p>Типичные уровни значимости:</p>
    <ul>
        <li>\(\alpha = 0.1\) (10&#37;)</li>
        <li>\(\alpha = 0.05\) (5&#37;)</li>
        <li>\(\alpha = 0.01\) (1&#37;)</li>
    </ul>
    <p>Более низкий уровень значимости означает, что доказательства в данных должны быть более убедительными, чтобы отвергнуть нулевую гипотезу. </p>
    <p>Не существует &quot;правильного&quot; уровня значимости - он лишь констатирует неопределенность вывода.</p>
    <div class="w3-panel w3-note">
        <p><b>Примечание:</b> 5&#37; уровень значимости означает, что когда мы отвергаем нулевую гипотезу:</p>
        <p>Мы ожидаем отклонить <b>истинную</b> нулевую гипотезу в 5 случаях из 100.</p>
    </div>
    <hr>
    <h2>4. Расчет статистики теста</h2>
    <p>Статистика теста используется для определения результата проверки гипотезы.</p>
    <p>Статистика теста - это <a href="statistics_standard_normal_distribution.php">стандартизованное</a> значение, рассчитанное на основе выборки.</p>
    <p>Формула для тестовой статистики (TS) среднего значения генеральной совокупности:</p>
    <p>\(\displaystyle \frac{\bar{x} - \mu}{s} \cdot \sqrt{n} \)</p>
    <p>\(\bar{x}-\mu\) - это <b>разница</b> между <b>выборкой</b> среднего (\(\bar{x}\)) и заявленным средним <b>совокупности</b> (\(\mu\)).</p>
    <p>\(s\) - это <a href="statistics_standard_deviation.php">стандартное отклонение выборки</a>.</p>
    <p>\(n\) - это размер выборки.</p>
    <p>В нашем примере:</p>
    <div class="w3-code notranslate">
        <p>Заявленное (\(H_{0}\)) среднее совокупности (\(\mu\)) было \( 55 \)</p>
        <p>Среднее выборки (\(\bar{x}\)) было \(62.1\)</p>
        <p>Стандартное отклонение выборки (\(s\)) было \(13.46\)</p>
        <p>Размер выборки (\(n\)) был \(30\)</p>
    </div>
    <p>Таким образом, тестовая статистика (TS) тогда:</p>
    <p>\(\displaystyle \frac{62.1-55}{13.46} \cdot \sqrt{30} = \frac{7.1}{13.46} \cdot \sqrt{30}  \approx 0.528 \cdot 5.477 = \underline{2.889}\)</p>
    <p>Вы также можете рассчитать статистику теста, используя функции языка программирования:</p>
    <div class="w3-example">
        <h3>Пример</h3>
        <p>В Python используйте библиотеки scipy и math для вычисления статистики теста.</p>
        <div class="w3-code notranslate pythonHigh">
            import scipy.stats as stats
            <br>
            import math
            <br>
            <br>
            # Укажите среднее значение выборки (x_bar), стандартное отклонение выборки, среднее значение, заявленное в нулевой гипотезе (mu_null), и размер выборки (n)
            <br>
            x_bar = 62.1
            <br>
            s = 13.46
            <br>
            mu_null = 55
            <br>
            n = 30
            <br>
            <br>
            # Рассчитайте и распечатайте тестовую статистику
            <br>
            print((x_bar - mu_null)/(s/math.sqrt(n)))
        </div>
