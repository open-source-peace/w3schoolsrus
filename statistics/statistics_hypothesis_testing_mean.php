    <h2>2. Определение претензий</h2>
    <p>Нам нужно определить <strong>нулевую гипотезу</strong> (\(H_{0}\)) и <strong>альтернативную гипотезу</strong> (\(H_{1}\)) на основе утверждения, которое мы проверяем.</p>
    <p>Претензия была:</p>
    <div class="w3-code notranslate">
        <p>"Средний возраст лауреатов Нобелевской премии, когда они получили премию, <b>более</b> чем 55 лет".</p>
    </div>
    <p>В этом случае <b>параметр</b> - это средний возраст лауреатов Нобелевской премии, когда они получили премию (\(\mu\)).</p>
    <p>Тогда нулевая и альтернативная гипотезы:</p>
    <div class="w3-code notranslate">
        <p><strong>Нулевая гипотеза</strong>: средний возраст - 55 лет.</p>
        <p><strong>Альтернативная гипотеза</strong>: средний возраст был <b>больше</b> чем 55 лет.</p>
    </div>
    <p>Что можно выразить символами как:</p>
    <div class="w3-code notranslate">
        <p>\(H_{0}\): \(\mu = 55 \)</p>
        <p>\(H_{1}\): \(\mu > 55 \)</p>
    </div>
    <p>Это '<b>правый</b> хвост' теста, поскольку альтернативная гипотеза утверждает, что пропорция <b>больше</b>, чем в нулевой гипотезе.</p>
    <p>Если данные подтверждают альтернативную гипотезу, мы <b>отклоняем</b> нулевую гипотезу и <b>принимаем</b> альтернативную гипотезу.</p>
