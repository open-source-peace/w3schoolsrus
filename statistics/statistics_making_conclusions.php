    <h2>Причинный вывод</h2>
    <p>Причинно-следственный вывод используется для расследования, если что-то вызывает другое.</p>
    <p>Например: дождь заставляет растения расти?</p>
    <p>Если мы считаем, что две вещи связаны, мы можем исследовать, <strong>коррелируют ли они</strong>. Статистические данные можно использовать, чтобы выяснить, насколько сильна эта связь.</p>
    <p>Даже если что-то взаимосвязано, выяснить что-то, вызванное другими вещами, может быть сложно. Это можно сделать с помощью хорошего <strong>экспериментального плана</strong> или других специальных статистических методов.</p>
