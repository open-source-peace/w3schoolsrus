    <h2>Статистические выводы</h2>
    <p>Статистика на основе данных в <strong>выборке</strong> используется для вывода обо всей <strong>генеральной совокупности</strong>. Это тип <strong>статистического вывода</strong>.</p>
    <p><strong>Теория вероятностей</strong> используется для расчета уверенности в том, что эти статистические данные также применимы к совокупности.</p>
    <p>При использовании выборки <b>всегда</b> будет некоторая неопределенность в отношении того, как выглядят данные для генеральной совокупности.</p>
    <p>Неопределенность часто выражается как <strong>доверительные интервалы</strong>.</p>
    <p><strong>Доверительные интервалы</strong> - это числовые способы показать, насколько вероятно, что <strong>истинное значение</strong> этой статистики находится в пределах определенного диапазона для совокупности.</p>
    <p><strong>Проверка гипотез</strong> - ещё один способ проверить, истинно ли утверждение о совокупности. Точнее, он проверяет, насколько вероятно, что гипотеза верна, на основе выборочных данных.</p>
    <p>Некоторые примеры утверждений или вопросов, которые можно проверить с помощью проверки гипотез:</p>
    <ul>
        <li>Люди в Нидерландах выше людей в Дании</li>
        <li>Люди предпочитают Пепси или Кока-колу?</li>
        <li>Лечит ли болезнь новое лекарство?</li>
    </ul>
