        <li>Гистограммы количественных данных</li>
        <li>Точечные диаграммы для двумерных данных</li>
    </ul>
    <p>Графики часто имеют тесную связь с числовой сводной статистикой.</p>
    <p>Например, прямоугольные диаграммы показывают, где находятся <strong>квартили</strong>.
    <p>Квартили также сообщают нам, где находятся минимальное и максимальное значения, диапазон, межквартильный диапазон и медиана.</p>
