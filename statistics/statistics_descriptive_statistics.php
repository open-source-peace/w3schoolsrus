    <h2>Ключевые особенности описания данных</h2>
    <p>Быстрый обзор того, как распределяются данные, является важным этапом в статистических методах.</p>
    <p>Мы вычисляем ключевые числовые значения данных, которые говорят нам о распределении данных. Мы также рисуем графики, наглядно показывающие, как распределяются данные.</p>
    <p>Основные характеристики данных:</p>
    <ul>
        <li>Где находится центр данных? (местонахождение)</li>
        <li>Насколько различаются данные? (масштаб)</li>
        <li>Какова форма данных? (форма)</li>
    </ul>
    <p>Их можно описать <strong>сводной статистикой</strong> (числовыми значениями).</p>
    <h3>Центр данных</h3>
    <p><strong>центр данных</strong>  - это место, где сосредоточено большинство значений.</p>
    <p>Различные типы средних значений, такие как среднее значение, медиана и режим, - это <strong>меры</strong> центра.</p>
    <div class="w3-panel w3-note">
        <p><b>Примечание:</b> Меры центра также называются <strong>параметрами местоположения</strong>, потому что они говорят нам кое-что о том, где данные &quot;расположены&quot; в числовой строке.</p>
    </div>
    <h3>Вариация данных</h3>
    <p><strong>Вариация</strong> данных - это то, насколько данные распределены вокруг центра.</p>
    <p>Такие статистические данные, как стандартное отклонение, диапазон и квартили, являются <strong>мерами</strong> вариации.</p>
    <div class="w3-panel w3-note">
        <p><b>Примечание.</b> Меры вариации также называются <strong>параметрами шкалы</strong>.</p>
    </div>
    <h3>Форма данных</h3>
    <p>Форма данных может указывать на то, как данные сгруппированы по обе стороны от центра.</p>
    <p>Статистика, такая как <strong>перекос</strong>, показывает, больше правая или левая часть центра. Наклон - это один из типов <strong>параметров формы</strong>.</p>
    <h3>Таблицы частот</h3>
    <p>Типичным представлением данных являются <strong>таблицы частот</strong>.</p>
    <p><strong>Таблица частот</strong> подсчитывает и упорядочивает данные в таблице. Обычно данные необходимо отсортировать по интервалам. </p>
    <p>Таблицы частот часто служат основой для построения графиков для визуального представления данных.</p>
