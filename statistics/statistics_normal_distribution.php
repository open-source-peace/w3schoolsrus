    <h2>Различные средние и стандартные отклонения</h2>
    <p>Среднее значение описывает, где находится центр нормального распределения.</p>
    <p>Вот график, показывающий три разных нормальных распределения с <b>одинаковым</b> стандартным отклонением, но разными средними значениями.</p>
    <p><img src="../svg/img_norm_means.svg" style="width:100%; max-width:1000px; height:auto" alt="Нормальные распределения с разными средствами"></p>
    <p>Стандартное отклонение описывает, насколько распространено нормальное распределение.</p>
    <p>Вот график, показывающий три разных нормальных распределения с <b>одинаковым</b> средним, но разными стандартными отклонениями.</p>
    <p><img src="../svg/img_norm_stds.svg" style="width:100%; max-width:1000px; height:auto" alt="Нормальные распределения с разными стандартными отклонениями"></p>
    <p>У фиолетовой кривой наибольшее стандартное отклонение, а у черной кривой наименьшее стандартное отклонение.</p>
    <p>Площадь под каждой кривой по-прежнему равна 1, или 100&#37;.</p>
