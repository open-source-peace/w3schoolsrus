    <h2>Пример нормально распределенных данных на реальных данных</h2>
    <p>Реальные данные часто распространяются нормально.</p>
    <p>Вот гистограмма возраста лауреатов Нобелевской премии, когда они выиграли эту премию:</p>
    <p><img src="../svg/img_nobel_norm.svg" style="width:100%; max-width:1000px; height:auto" alt="Гистограмма возраста лауреатов Нобелевской премии, когда они выиграли премию, и нормальное распределение, соответствующее данным"></p>
    <p>Нормальное распределение, нарисованное в верхней части гистограммы, основано на среднем значении генеральной совокупности (\(\mu\)) и стандартном отклонении (\(\sigma\)) реальных данных.</p>
    <p>Мы видим, что гистограмма близка к нормальному распределению.</p>
    <p>Примеры реальных переменных, которые могут иметь нормальное распределение:</p>
