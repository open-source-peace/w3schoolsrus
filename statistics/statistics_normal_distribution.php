    <h2>Нормальное распределение</h2>
    <p><strong>Нормальное распределение</strong> описывается <a href="statistics_mean.php">средним</a> (\(\mu\)) и <a href="statistics_standard_deviation.php">стандартным отклонением</a> (\(\sigma\)).</p>
    <p>Нормальное распределение часто называют &quot;колоколообразной кривой&quot; из-за его формы:</p>
    <ul>
        <li>Большинство значений около центра (\(\mu\))</li>
        <li><a href="statistics_median.php">Медиана</a> и среднее значение равны</li>
        <li>У него только один <a href="statistics_mode.php">режим</a></li>
        <li>Он симметричен, то есть уменьшает на одинаковую величину слева и справа от центра.</li>
    </ul>
    <p>Площадь под кривой нормального распределения представляет вероятности для данных.</p>
    <p>Площадь под всей кривой равна 1, или 100&#37;</p>
    <p>Вот график нормального распределения с вероятностями между стандартными отклонениями (\(\sigma\)):</p>
    <p><img src="img_normal_distribution.svg" style="width:100%; max-width:1000px; height:auto" alt="Нормальные распределения с указанными вероятностями"></p>
    <ul>
        <li>Примерно 68.3&#37; данных находится в пределах 1 стандартного отклонения от среднего (от &mu;-1&sigma; to &mu;+1&sigma;) </li>
        <li>Примерно 95.5&#37; данных находится в пределах 2 стандартных отклонений от среднего (от &mu;-2&sigma; to &mu;+2&sigma;) </li>
        <li>Примерно 99.7&#37; данных находится в пределах 3 стандартных отклонений от среднего (от &mu;-3&sigma; to &mu;+3&sigma;) </li>
    </ul>
    <div class="w3-panel w3-note">
        <p><b>Примечание:</b> Вероятности нормального распределения можно рассчитать только для интервалов (между двумя значениями).</p>
    </div>
