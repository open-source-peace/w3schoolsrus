    <h2>Распределение вероятностей</h2>
    <p>Распределения вероятностей - это функции, которые вычисляют вероятности исходов случайных величин.</p>
    <p>Типичными примерами случайных величин являются подбрасывание монет и костей.</p>
    <p>Вот график, показывающий результаты растущего числа подбрасываний монет и ожидаемые значения результатов (орел или решка).</p>
    <p>Ожидаемые значения подбрасывания монеты - это распределение вероятностей подбрасывания монеты.</p>
    <p><img src="../images/img_1_2dice_100f2.gif" style="width:100%; max-width:800px; height:auto" alt="Имитация подбрасывания монет и ожидаемые значения"></p>
    <p>Обратите внимание, как результат случайных подбрасываний монет приближается к ожидаемым значениям (50&#37;) по мере увеличения количества подбрасываний.</p>
    <p>Точно так же график, показывающий результаты растущего числа бросков кубиков и ожидаемые значения результатов (от 1 до 6).</p>
    <p><img src="../images/img_1_6dice_100f2.gif" style="width:100%; max-width:800px; height:auto" alt="Имитация бросков кубиков и ожидаемые значения"></p>
    <p>Ещё раз обратите внимание, как результат случайных бросков кубиков приближается к ожидаемым значениям (1/6, или 16,666&#37;) по мере увеличения количества бросков.</p>
    <p>Когда случайная величина представляет собой <b>сумму</b> кубиков, результаты и ожидаемые значения принимают другую форму.</p>
    <p>Разная форма возникает из-за того, что существует больше способов получить сумму, близкую к середине, чем небольшую или большую сумму.</p>
    <p><img src="../images/img_2_6dice_100f2.gif" style="width:100%; max-width:800px; height:auto" alt="Смоделированная сумма двух бросков кубиков и ожидаемые значения"></p>
    <p>По мере того, как мы продолжаем увеличивать количество кубиков для получения суммы, форма результатов и ожидаемых значений становится все больше и больше похожа на нормальное распределение.</p>
    <p><img src="../images/img_3_6dice_100f2.gif" style="width:50%; max-width:600px; height:auto" alt="Смоделированная сумма 3 бросков кубиков и ожидаемые значения"><img src="../images/img_5_6dice_100f2.gif" style="width:50%; max-width:600px; height:auto" alt="Смоделированная сумма 5 бросков кубиков и ожидаемые значения"></p>
    <p>Многие переменные реального мира следуют аналогичному шаблону и, естественно, образуют нормальные распределения.</p>
    <p>Обычно распределенные переменные можно анализировать с помощью хорошо известных методов.</p>
