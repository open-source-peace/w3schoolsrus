    <h2>Статистика теста </h2>
    <p>Статистика теста используется для определения результата проверки гипотезы.</p>
    <p>Статистика теста - это <strong>стандартизированное</strong> значение, рассчитанное на основе выборки.</p>
    <p>Стандартизация означает преобразование статистики в хорошо известное <strong>распределение вероятностей</strong>.</p>
    <p>Тип распределения вероятностей зависит от типа теста.</p>
    <p>Распространенными примерами являются:</p>
    <ul>
        <li><a href="statistics_standard_normal_distribution.php">Стандартное нормальное распределение</a> (Z): используется для <a href="statistics_hypothesis_testing_proportion.php">Проверки пропорций совокупности</a></li>
        <li><a href="statistics_students_t_distribution.php">Стьюдент T-распределение</a> (T): используется для <a href="statistics_hypothesis_testing_mean.php">Проверки среднего совокупности</a></li>
    </ul>
    <div class="w3-panel w3-note">
        <p><b>Примечание:</b> Вы узнаете, как рассчитать статистику теста для каждого типа теста, в следующих главах.</p>
    </div>
