    <p class="intro"><strong>Проверка гипотез</strong> - это формальный способ проверить, верна ли гипотеза о <a href="statistics_populations_and_samples.php">совокупности</a>.</p>
    <hr>
    <h2>Проверка гипотез</h2>
    <p><strong>Гипотеза</strong> - это утверждение о <a href="statistics_parameters_and_statistics.php">параметре</a> совокупности.</p>
    <p><strong>Проверка гипотез</strong> - это формальная процедура, позволяющая проверить, верна ли гипотеза.</p>
    <p>Примеры претензий, которые можно проверить:</p>
    <div class="w3-code notranslate">
        <p>Средний рост жителей Дании <b>больше</b> 170 см.</p>
        <p>Доля левшей в Австралии <b>не</b> 10%.</p>
        <p>Средний доход стоматолога <b>меньше</b> среднего дохода стоматолога.</p>
    </div>
    <hr>
    <h2>Нулевая и альтернативная гипотеза</h2>
    <p>Проверка гипотез основывается на двух разных утверждениях о параметре генеральной совокупности.</p>
    <p><strong>Нулевая</strong> гипотеза (\(H_{0} \)) и <strong>альтернативная</strong> гипотеза (\(H_{1}\)) являются утверждениями.</p>
    <p>Эти два утверждения должны быть <strong>взаимоисключающими</strong>, то есть только одно из них может быть истинным.</p>
    <p>Обычно мы пытаемся доказать альтернативную гипотезу.</p>
    <p>Например, мы хотим проверить следующее утверждение:</p>
    <div class="w3-code notranslate">
        <p>&quot;Средний рост людей в Дании более 170 см&quot;.</p>
    </div>
    <p>В данном случае <strong>параметр</strong> - это средний рост людей в Дании (\(\mu\)).</p>
    <p>Нулевая и альтернативная гипотеза будет:</p>
    <div class="w3-code notranslate">
        <p><strong>Нулевая гипотеза</strong>: средний рост жителей Дании <b>составляет</b> 170 см.</p>
        <p><strong>Альтернативная гипотеза</strong>: средний рост жителей Дании <b>больше</b> 170 см.</p>
    </div>
    <p>Заявления часто выражаются такими символами:</p>
    <div class="w3-code notranslate">
        <p>\(H_{0}\): \(\mu = 170 \: cm \)</p>
        <p>\(H_{1}\): \(\mu > 170 \: cm \)</p>
    </div>
    <p>Если данные подтверждают альтернативную гипотезу, мы <b>отклоняем</b> нулевую гипотезу и <b>принимаем</b> альтернативную гипотезу.</p>
    <p>Если данные <b>не</b> подтверждают альтернативную гипотезу, мы <b>сохраняем</b> нулевую гипотезу.</p>
    <div class="w3-panel w3-note">
        <p><b>Примечание:</b> Альтернативная гипотеза также упоминается как \(H_{A}\)</code></p>
    </div>
