    <h2>Подходы критического значения и P-значения</h2>
    <p>Для проверки гипотез используются два основных подхода:</p>
    <ul>
        <li>Подход <strong>критического значения</strong> сравнивает статистику теста с критическим значением уровня значимости.</li>
        <li>Подход <strong>p-значения</strong> сравнивает p-значение тестовой статистики и уровень значимости.</li>
    </ul>
    <h3>Подход критического значения</h3>
    <p>Подход критического значения проверяет, находится ли тестовая статистика в <strong>области отклонения</strong>.</p>
    <p>Область отклонения - это область вероятности в хвостах распределения.</p>
    <p>Размер области отклонения определяется уровнем значимости (\(\alpha\)).</p>
    <p>Значение, отделяющее область отклонения от остальных, называется <strong>критическим значением</strong>.</p>
    <p>Вот графическая иллюстрация:</p>
    <p><img src="../svg/img_critical_value_right.svg" style="width:100%; max-width:1000px; height:auto" alt="График Т-распределения для правостороннего теста, области отклонения (alpha), критического значения и статистики теста в области отклонения"></p>
    <p>Если статистика теста находится <b>внутри</b> этой области отклонения, нулевая гипотеза <b>отклоняется</b>.</p>
    <p>Например, если тестовая статистика составляет 2,3, а критическое значение равно 2 для уровня значимости (\(\alpha = 0.05\)):</p>
    <div class="w3-code notranslate">
        <p>Мы отклоняем нулевую гипотезу (\(H_{0} \)) на уровне значимости 0.05 (\(\alpha\))</p>
    </div>
    <h3>Подход P-значения</h3>
    <p>Подход с p-значением проверяет, является ли p-значение тестовой статистики <b>меньше</b>, чем уровень значимости (\(\alpha\)).</p>
    <p>P-значение тестовой статистики - это область вероятности в хвостах распределения от значения тестовой статистики.</p>
    <p>Вот графическая иллюстрация:</p>
    <p><img src="../svg/img_p_value_right.svg"  style="width:100%; max-width:1000px; height:auto" alt="Графики Т-распределений для правостороннего теста с площадью хвоста (alpha) и площадью хвоста, равной p-значению статистики теста"></p>
    <p>Если p-значение <b>меньше</b> уровня значимости, нулевая гипотеза <b>отклоняется</b>.</p>
    <p>P-значение напрямую указывает нам <strong>самый низкий уровень значимости</strong>, на котором мы можем отклонить нулевую гипотезу.</p>
    <p>Например, если p-значение равно 0,03:</p>
    <div class="w3-code notranslate">
        <p>Мы отклоняем нулевую гипотезу (\(H_{0} \)) на уровне значимости 0,05 (\(\alpha\))</p>
        <p>Мы сохраняем нулевую гипотезу (\(H_{0}\)) на уровне значимости 0,01 (\(\alpha\))</p>
    </div>
    <div class="w3-panel w3-note">
        <p><b>Примечание:</b> Эти два подхода отличаются только тем, как они представляют заключение.</p>
    </div>
