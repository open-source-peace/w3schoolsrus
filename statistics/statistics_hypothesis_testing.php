    <h2>Этапы проверки гипотез</h2>
    <p>Для проверки гипотез используются следующие шаги:</p>
    <ol>
        <li>Проверить условия</li>
        <li>Определить претензииь</li>
        <li>Определить уровень значимости</li>
        <li>Рассчитать статистику теста</li>
        <li>Заключение</li>
    </ol>
    <p>Одно <b>условие</b> заключается в том, что выборка производится <a href="statistics_sample_types.php">случайным образом</a> из генеральной совокупности.</p>
    <p>Остальные условия зависят от того, для какого типа параметра вы проверяете гипотезу.</p>
    <p>Общие параметры для проверки гипотез:</p>
    <ul>
        <li>Пропорции (для качественных данных)</li>
        <li>Средние значения (для числовых данных)</li>
    </ul>
