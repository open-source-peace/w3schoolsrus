    <h2>Сбор данных</h2>
    <p><strong>Сбор данных</strong> - это первый шаг в статистическом анализе.</p>
    <p>Например, вы хотите узнать что-то обо <b>всех людях во Франции</b>.</p>
    <p><strong>Совокупность</strong> - это все жители Франции.</p>
    <p>Слишком много усилий для сбора информации обо всех членах совокупности (например, обо всех более чем 67 миллионах человек, живущих во Франции). Часто бывает намного проще собрать меньшую группу из этой совокупности и проанализировать её. Это называется <strong>выборка</strong> (которая используется как образец).</p>
    <?php include '../include/addown_content.php'; ?>
    <h3>Репрезентативная выборка</h3>
    <p>Выборка должна быть <strong>похожей</strong> на всё население Франции. Она должна иметь те же характеристики, что и совокупность (всё население). Если вы включите только 48-летних людей по имени Жак, проживающих в Париже, выборка не будет похожа на всю совокупность.</p>
    <p>Итак, для хорошей выборки вам понадобятся люди со всей Франции, разных возрастов, профессий и т.д.</p>
    <p>Если члены выборки имеют такие же характеристики (например, возраст, профессия и т.д.), что и всё население Франции, мы говорим, что выборка является <strong>репрезентативной</strong> для совокупности.</p>
    <p>Хорошая <strong>репрезентативная выборка</strong> имеет решающее значение для статистических методов.</p>
