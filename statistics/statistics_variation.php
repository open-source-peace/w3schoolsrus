    <h2>Межквартильный диапазон</h2>
    <p><strong>Межквартильный диапазон</strong> - это разница между первым и третьим квартилями (Q<sub>1</sub> и Q<sub>3</sub>).</p>
    <p>&quot;Средняя половина&quot; данных находится между первым и третьим квартилем.</p>
    <p>Вот гистограмма возраста всех 934 лауреатов Нобелевской премии до 2020 года, показывающая <strong>межквартильный диапазон (IQR)</strong>:</p>
    <p><img src="../svg/img_histogram_iqr.svg" style="width:100%; max-width:1000px; height:auto" alt="Гистограмма возраста лауреатов Нобелевской премии с указанием межквартильного диапазона"></p>
    <p>Здесь средняя половина - от 51 до 69 лет. Тогда межквартильный диапазон для лауреатов Нобелевской премии составляет 18 лет.</p>
    <hr>
    <h2>Стандартное отклонение</h2>
    <p><strong>Стандартное отклонение</strong> - наиболее часто используемый показатель вариации.</p>
    <p>Стандартное отклонение (&sigma;) измеряет, насколько &quot;типичное&quot; наблюдение отличается от среднего значения данных (&mu;).</p>
    <p>Стандартное отклонение важно для многих статистических методов.</p>
    <p>Вот гистограмма возраста всех 934 лауреатов Нобелевской премии до 2020 года, показывающая <strong>стандартные отклонения</strong>:</p>
    <p><img src="../svg/img_histogram_std.svg" style="width:100%; max-width:1000px; height:auto" alt="Гистограмма возраста лауреатов Нобелевской премии с указанием межквартильного диапазона"></p>
    <div class="w3-panel w3-note">
        <p><b>Примечание:</b> Значения в пределах одного стандартного отклонения (&sigma;) считаются типичными.</p>
        <p>Значения за пределами трех стандартных отклонений считаются <strong>выбросами</strong>.</p>
