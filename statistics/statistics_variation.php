    <h2>Вариация данных</h2>
    <p><strong>Меры вариации</strong> - это статистические данные о том, насколько далеко друг от друга значения в наблюдениях (точки данных).</p>
    <p>Существуют разные меры вариации. Чаще всего используются:</p>
    <ul>
        <li><a href="statistics_range.php">Диапазон</a></li>
        <li><a href="statistics_quartiles_and_percentiles.php">Квартили и процентили</a></li>
        <li><a href="statistics_interquartile_range.php">Межквартильный диапазон</a></li>
        <li><a href="statistics_standard_deviation.php">Стандартное отклонение</a></li>
    </ul>
    <p>Меры вариации в сочетании со средним значением (мера центра) дают хорошее представление о распределении данных.</p>
    <div class="w3-note">
        <p><b>Примечание:</b> Эти меры вариации могут быть использованы только для числовых данных.</p>
    </div>
    <hr>
    <h2>Диапазон</h2>
    <p><strong>Диапазон</strong> - это разница между наименьшим и наибольшим значением данных.</p>
    <p>Диапазон - это простейшая мера вариации.</p>
    <p>Вот гистограмма возраста всех 934 лауреатов Нобелевской премии до 2020 года, показывающая <strong>диапазон</strong>:</p>
    <p><img src="../svg/img_histogram_range.svg" style="width:100%; max-width:1000px; height:auto" alt="Гистограмма возраста лауреатов Нобелевской премии с диапазоном между минимальным и максимальным значениями"></p>
    <p>Самому молодому победителю было 17 лет, а самому старшему - 97 лет. Тогда диапазон возраста для лауреатов Нобелевской премии составляет 80 лет.</p>
    <hr>
    <h2>Квартили и процентили</h2>
    <p><strong>Квартили и процентили</strong> - это способы разделения равного количества значений в данных на части.</p>
    <p><strong>Квартили</strong> - это значения, которые разделяют данные на четыре равные части.</p>
    <p><strong>Процентили</strong> - это значения, которые разделяют данные на 100 равных частей.</p>
    <p>Вот гистограмма возраста всех 934 лауреатов Нобелевской премии до 2020 года, показывающая <strong>квартили</strong>:</p>
    <p><img src="../svg/img_histogram_quartiles.svg" style="width:100%; max-width:1000px; height:auto" alt="Гистограмма возраста лауреатов Нобелевской премии с указанием квартилей"></p>
    <p>Квартили (Q<sub>0</sub>,Q<sub>1</sub>,Q<sub>2</sub>,Q<sub>3</sub>,Q<sub>4</sub>) - это значения, которые разделяют каждую четверть.</p>
    <p>Между Q<sub>0</sub> и Q<sub>1</sub> находятся 25&#37; самые низкие значения в данных. Между Q<sub>1</sub> и Q<sub>2</sub> находятся следующие 25%. И так далее.</p>
    <ul>
        <li>Q<sub>0</sub> это наименьшее значение в данных</li>
        <li>Q<sub>2</sub> это среднее значение (медиана)</li>
        <li>Q<sub>4</sub> это наибольшее значение в данных</li>
    </ul>
