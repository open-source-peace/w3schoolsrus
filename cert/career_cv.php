      <h2>Квалификация и навыки</h2>
      <p>Навыки лучше всего описывать списком.</p>
      <p>Укажите в резюме только самые важные навыки (качества). Перечислите как профессиональные, так и личные качества.</p>
      <p>Хард-скилы (профессиональные качества) - это конкретные технические знания или навыки, которые вы приобрели. Вот несколько примеров:</p>
      <ul>
          <li>Управление базой данных</li>
          <li>Сетевая безопасность</li>
          <li>SEO-маркетинг</li>
          <li>Наука о данных</li>
          <li>Мобильная разработка</li>
          <li>UI/UX дизайн</li>
          <li>Языки программирования или технологии (например, HTML, CSS, JavaScript и Python)</li>
      </ul>
      <p>Софт-скилы (личные качества) - это черты личности, которые определяют то, как вы работаете самостоятельно и с другими. Вот несколько примеров:</p>
      <ul>
          <li>Эффективное общение</li>
          <li>Надежность</li>
          <li>Активное слушание</li>
          <li>Открытость</li>
          <li>Работа в команде</li>
          <li>Творчество</li>
          <li>Решение проблем</li>
          <li>Критическое мышление</li>
          <li>Сочувствие</li>
      </ul>
