    <h2>Технический комитет ECMA 39</h2>
    <p>В 1996 году Netscape и Брендан Эйх передали JavaScript в международную организацию стандартизации ECMA, и для разработки языка был создан технический комитет (TC39).</p>
    <p>ECMA-262 Edition 1 был выпущен в июне 1997 г.</p>
    <hr>
    <?php include '../include/adinfeed.php'; ?>

    <h2>От ES4 до ES6</h2>
    <p>Когда комитет TC39 собрался в Осло в 2008 году, чтобы согласовать ECMAScript 4, они разделились на 2 очень разных лагеря:</p>
    <p><b>Лагерь ECMAScript 3.1</b>:<br>Microsoft и Yahoo хотели получить постепенное обновление с ES3.</p>
    <p><b>Лагерь ECMAScript 4</b>:<br>Adobe, Mozilla, Opera и Google, которые хотели масштабного обновления ES4.</p>
    <p>13 августа 2008 г. Брендан Эйх написал <a target="_blank" href="https://mail.mozilla.org/pipermail/es-discuss/2008-August/006837.html">email</a>:</p>
    <p class="w3-serif w3-large"><i>Ни для кого не секрет, что орган по стандартизации JavaScript, технический комитет Ecma 39 был разделен на более чем год, при этом некоторые члены отдали предпочтение ES4, главному четвертому изданию ECMA-262, а другие выступали за ES3.1 на основе существующего ECMA-262. Спецификация Edition 3 (ES3). Рад сообщить, что раскол окончен.</i></p>
    <p>Решение заключалось в совместной работе:</p>
    <ul>
        <li>ECMAScript 4 был переименован в ES5</li>
        <li>ES5 должен быть постепенным обновлением ECMAScript 3</li>
        <li>Возможности ECMAScript 4 следует использовать в более поздних версиях</li>
        <li>TC39 следует разработать новый основной выпуск, больший по объему, чем ES5</li>
    </ul>
    <p>Запланированный новый выпуск (ES6) имел кодовое название "Harmony" (из-за созданного им раскола?).</p>
    <p><strong>ES5</strong> имел огромный успех. Он был выпущен в 2009 году, и к июлю 2013 года все основные браузеры (включая Internet Explorer) были полностью совместимы с ним:</p>
