    <h2>Старые приложения</h2>
    <p>Многие старые компьютеры (например, в публичных библиотеках) всё ещё используют Internet Explorer.</p>
    <p>Многие устаревшие веб-приложения используют Internet Explorer.</p>
    <p>Многие устаревшие веб-приложения используют библиотеки времени выполнения Internet Explorer.</p>
    <p>Многие приложения на основе AJAX используют компоненты Microsoft ActiveX.</p>
    <p>Чтобы упростить переход с Internet Explorer, Microsoft Edge предлагает <b>Internet Explorer Mode</b>, обеспечивающий обратную совместимость и позволяющий клиентам продолжать запускать устаревшие веб-приложения.</p>
