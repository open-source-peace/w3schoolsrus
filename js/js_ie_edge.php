    <h2>Microsoft Edge Legacy</h2>
    <p>Edge был браузером по умолчанию для Windows 10.</p>
    <p>Он был построен с помощью движка браузера Microsoft <strong>EdgeHTML</strong> и их движка JavaScript <b>Chakra</b>.</p>
    <p>Первые версии Edge (12-18) теперь называются &quot;<strong>Edge Legacy</strong>&quot;.</p>
    <p>Поддержка Microsoft Edge Legacy закончилась 9 марта 2021 г.</p>
    <p>В будущих обновлениях Windows Edge Legacy будет заменен на <strong>The New Edge</strong>.</p>
