    <h2>Новый Edge</h2>
    <p>Новый Microsoft Edge основан на <b>Chromium</b> с движками <b>Blink</b> и <b>V8</b>.</p>
    <p>Он был выпущен в январе 2020 года и доступен для Windows 7, 8 и 10.</p>
    <p>Его также можно загрузить для macOS, iOS и Android.</p>
    <p>Новый Edge следует <b>Политике современного жизненного цикла</b> (обновления примерно каждые шесть недель).</p>
    <hr>
