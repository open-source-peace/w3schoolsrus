    <h2>ASP.NET веб-страницы</h2>
    <p>Веб-страницы ASP.NET - это модель приложения SPA - Single Page Application (одностраничное приложение).</p>
    <p>Модель SPA очень похожа на PHP и Classic ASP.</p>
    <p>Веб-страницы ASP.NET объединяются в новое ядро ASP.NET.</p>
    <a class="ws-btn" href="../asp/webpages_intro.php">Посетить учебник Веб-страницы &raquo;</a>
    <hr>

    <h2>ASP.NET MVC</h2>
    <p>ASP.NET MVC - это модель приложения MVC (Model-View-Controller - модель-представление-контроллер).</p>
    <p>ASP.NET MVC объединяется с новым ядром ASP.NET.</p>
    <p>ASP.NET MVC не рассматривается в этом учебнике.</p>
    <hr>

    <h2>ASP.NET Web API</h2>
    <p>ASP.NET API - это модель приложения API (Application Programming Interface - интерфейс прикладного программирования).</p>
    <p>ASP.NET API объединяется с новым ASP.NET ядром.</p>
    <p>ASP.NET API не рассматривается в этом учебнике.</p>
    <hr>

    <h2>ASP.NET Веб-формы</h2>
    <p>Веб-формы ASP.NET - это модель приложения, управляемая событиями.</p>
    <p>Веб-формы ASP.NET <b>не являются</b> частью нового ядра ASP.NET.</p>
    <p>ASP.NET веб-формы не рассматриваются в этом учебнике.</p>
    <hr>

    <h2>ASP.NET Core</h2>
    <p>ASP.NET Core было выпущено в 2016 году.</p>
    <p>ASP.NET Core объединяет ASP.NET MVC, веб-API ASP.NET и веб-страницы ASP.NET в одну платформу приложения.</p>
    <p>ASP.NET Core не рассматривается в этом учебнике.</p>
