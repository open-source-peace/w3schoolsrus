    <h2>ASP Технология</h2>
    <p>ASP и ASP.NET - это серверные технологии.</p>
    <p>Обе технологии позволяют выполнять компьютерный код на Интернет-сервере.</p>
    <p>Когда браузер запрашивает файл ASP или ASP.NET, механизм ASP читает файл, выполняет любой код в файле и возвращает результат в браузер.</p>
    <hr>

    <h2>Classic ASP - Active Server Pages</h2>
    <p>ASP (aka Classic ASP - классический ASP) был представлен в 1998 году как первый серверный язык сценариев Microsoft.</p>
    <p>Классические страницы ASP имеют расширение файла <strong>.asp</strong> и обычно пишутся на языке VBScript.</p>
    <a class="ws-btn" href="../asp/asp_introduction.php">Посетить учебник Classic ASP &raquo;</a>
    <hr>

    <h2>ASP.NET</h2>
    <p>ASP.NET был выпущен в 2002 году как преемник Classic ASP.</p>
    <p>Страницы ASP.NET имеют расширение <strong>.aspx</strong> и обычно написаны на C# (C sharp).</p>
    <p>ASP.NET 4.6 - последняя официальная версия ASP.NET.</p>
    <p>Ожидалось, что ASP.NET 5 станет важной переработкой ASP.NET.</p>
    <p>Однако разработка ASP.NET 5 была остановлена в пользу <a href="https://www.hanselman.com/blog/ASPNET5IsDeadIntroducingASPNETCore10AndNETCore10.aspx" target="_blank" rel="nofollow">ASP.NET Core</a>.</p>
