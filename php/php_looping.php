    <h2>PHP Циклы</h2>
    <p>Часто, когда вы пишете код, вам нужно, чтобы один и тот же блок кода повторялся снова и снова определенное количество раз. Т.о., вместо добавления нескольких почти одинаковых строк кода в скрипт мы можем использовать циклы.</p>
    <p>Циклы используются для выполнения одного и того же блока кода снова и снова, пока выполняется определенное условие.</p>
    <p>В PHP есть следующие типы циклов:</p>
    <ul>
        <li><code class="w3-codespan">while</code> - перебирает блок кода, пока выполняется указанное условие</li>
        <li><code class="w3-codespan">do...while</code> - один раз перебирает блок кода, а затем повторяет цикл, пока выполняется указанное условие</li>
        <li><code class="w3-codespan">for</code> - перебирает блок кода указанное количество раз</li>
        <li><code class="w3-codespan">foreach</code> - перебирает блок кода для каждого элемента в массиве</li>
    </ul>
    <p>В следующих главах будут объяснены и приведены примеры каждого типа цикла.</p>
