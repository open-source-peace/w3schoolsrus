    <p>Римляне использовали систему <b>Base 10</b>. Возможно, потому что они использовали свои 10 пальцев для счета.</p>
    <p>Сегодня в большинстве стран мира используется система счисления <b>Base 10</b>: западные числа, китайские числа, числа Брахми, греческие числа, числа на иврите...</p>
    <p>
        5 означает 5<br>
