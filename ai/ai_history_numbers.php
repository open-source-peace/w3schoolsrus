    <h2>Цифровые числа (Base 2)</h2>
    <p>Люди умеют считать с помощью десятичных чисел, компьютеры - нет.</p>
    <p>Электричество не может считать. Электричество может быть только включено или выключено.</p>
    <p>Компьютеры используют электричество или магнетизм для обозначения включения и выключения.</p>
    <p>Компьютеры используют <b>цифровые числа</b>, например 11100110 (в отличие от <b>чисел Base 10</b>, например 230).</p>
    <p>Цифровые числа, такие как 11100110, также называются <b>двоичными числами</b>.</p>
