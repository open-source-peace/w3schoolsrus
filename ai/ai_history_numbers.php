    <h2>Вавилонские числа (Base 60)</h2>
    <p>Мы считаем, что вавилоняне начали разработку сложного счета.</p>

    <a title="Josell7 / CC BY-SA (https://creativecommons.org/licenses/by-sa/4.0)"
       target="_blank" href="https://commons.wikimedia.org/wiki/File:Babylonian_numerals.svg">
        <img style="width:100%;max-width:600px" alt="Babylonian Numerals"
             src="../images/img_babylonian_numerals.jpg"></a>

    <p>В вавилонской системе счисления 60 различных цифр. Это система <b>Base 60</b>:</p>
    <p>1,5 означает 1 умножить на 60 плюс 5 (65)<br>3,30 означает 3 раза по 60 плюс 30 (180 + 30 = 210)</p>
    <p>У вавилонян была система счисления, в которой цифры слева представляли полные значения, как в нашей десятичной системе.</p>

    <p>Причина, по которой вавилоняне использовали 60 в качестве основы, заключалась (нам нравится верить), что 60 делится на большинство чисел: 1,2,3,4,5,6,10,12,15,20,30 и 60.</p>
    <p>Обратной стороной было то, что пользователю приходилось запоминать 60 разных цифр. Но система была умной. Многие из них все ещё используются сегодня:</p>
