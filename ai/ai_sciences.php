        <h2>Машинное обучение (ML)</h2>
        <p>ML (от анг. Machine Learning - Машинное обучение) - это создание компьютерных алгоритмов, которые могут улучшить самостоятельно.</p>
        <p>ML - это обучение алгоритмов, позволяющих делать прогнозы, например, определять, какое видео вам нравится на основе видео, которое вы просмотрели.</p>
        <h3>ML (Под присмотром)</h3>
        <p>Контролируемое машинное обучение (под присмотром) - используется для прогнозирования определенных отношений, например "Что такое спам в электронной почте".</p>
        <h3>AutoML (Без присмотра)</h3>
        <p>Неконтролируемое машинное обучение используется для прогнозирования неопределенных отношений, таких как значимые шаблоны в данных.</p>
        <p>Ожидается, что машинное обучение (ML) перейдет на AutoML, чтобы программисты могли решать проблемы без создания моделей.</p>
        <hr>

        <h2>Глубокое обучение</h2>
        <p>Глубокое обучение использует компьютерные алгоритмы, которые используют несколько уровней для извлечения данных более высокого уровня.</p>
        <p>Например, при оптическом считывании используются нижние слои для идентификации краев и более высокие слои для идентификации букв.</p>
