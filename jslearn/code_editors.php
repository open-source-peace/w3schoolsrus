    <p class='intro'>Большую часть своего рабочего времени программисты проводят в редакторах кода.</p>
    <p class='intro'>Есть два основных типа редакторов: <strong>IDE</strong> и <strong>&quot;лёгкие&quot; редакторы кода</strong>. Многие используют по одному инструменту каждого типа.</p>
  <hr>
  <h2>IDE</h2>
  <p>Термином <a href="https://ru.wikipedia.org/wiki/Интегрированная_среда_разработки" target="_blank" rel="nofollow" title="Смотреть на Википедии"><strong>IDE</strong></a> (Integrated Development Environment, «интегрированная среда разработки») называют мощные редакторы с множеством функций, которые работают в рамках целого проекта. Как видно из названия, это не просто редактор, а нечто большее.</p>
  <p>IDE загружает проект (который может состоять из множества файлов), позволяет переключаться между файлами, предлагает автодополнение по коду всего проекта (а не только открытого файла), также она интегрирована с системой контроля версий (например, такой как git), средой для тестирования и другими инструментами на уровне всего проекта.</p>
  <p>Если вы ещё не выбрали себе IDE, присмотритесь к этим:</p>
  <ul>
    <li><a href="https://code.visualstudio.com/" target="_blank" rel="nofollow" title="Перейти на официальный сайт">Visual Studio Code</a> (бесплатно).</li>
    <li><a href="https://www.jetbrains.com/webstorm/" target="_blank" rel="nofollow" title="Перейти на официальный сайт">WebStorm</a> (30 дней - триал, далее - платно).</li>
  </ul>
  <p>Обе IDE – кроссплатформенные.</p>
  <p>Для Windows есть ещё Visual Studio (не путать с Visual Studio Code). Visual Studio – это платная мощная среда разработки, которая работает только на Windows. Она хорошо подходит для .NET платформы. У неё есть бесплатная версия, которая называется
      <a href="https://visualstudio.microsoft.com/vs/community/" target="_blank" rel="nofollow" title="Перейти на официальный сайт">Visual Studio Community</a>.</p>
  <p>Многие IDE платные, но у них есть пробный период. Их цена обычно незначительна по сравнению с зарплатой квалифицированного разработчика, так что пробуйте и выбирайте ту, что вам подходит лучше других.</p>
  <hr>
  <h2>&quot;Лёгкие&quot; редакторы кода</h2>
  <p>&quot;Лёгкие&quot; редакторы менее мощные, чем IDE, но они отличаются скоростью, удобным интерфейсом и простотой.</p>
  <p>В основном их используют для того, чтобы быстро открыть и отредактировать нужный файл.</p>
  <p>Главное отличие между «лёгким» редактором и IDE состоит в том, что IDE работает на уровне целого проекта, поэтому она загружает больше данных при запуске, анализирует структуру проекта, если это необходимо, и так далее. Если вы работаете только с одним файлом, то гораздо быстрее открыть его в «лёгком» редакторе.</p>
  <p>На практике «лёгкие» редакторы могут иметь множество плагинов, включая автодополнение и анализаторы синтаксиса на уровне директории, поэтому границы между IDE и «лёгкими» редакторами размыты.</p>
  <p>Следующие варианты редакторов кода заслуживают вашего внимания:</p>
  <ul>
      <li><a href="https://atom.io/" target="_blank" rel="nofollow" title="Перейти на официальный сайт">Atom</a> (кроссплатформенный, бесплатный).</li>
      <li><a href="http://brackets.io/" target="_blank" rel="nofollow" title="Перейти на официальный сайт">Brackets</a> (кроссплатформенный, бесплатный).</li>
      <li><a href="https://www.sublimetext.com/" target="_blank" rel="nofollow" title="Перейти на официальный сайт">Sublime Text</a> (кроссплатформенный, условно-бесплатный).</li>
      <li><a href="https://notepad-plus-plus.org/downloads/" target="_blank" rel="nofollow" title="Перейти на официальный сайт">Notepad++</a> (Windows, бесплатный).</li>
      <li><a href="https://www.vim.org/" target="_blank" rel="nofollow" title="Перейти на официальный сайт">Vim</a> и <a href="https://www.gnu.org/software/emacs/" target="_blank" rel="nofollow" title="Перейти на официальный сайт">Emacs</a> тоже хороши, если знать, как ими пользоваться.</li>
