    <p>При стилизации html-страниц часто необходимо использовать различные цвета. Согласно спецификации <strong>HTML5</strong> вся стилизация страниц производится в каскадных таблицах стилей - <strong>CSS</strong>. Обычно на сайтах применяют подключение к html-страницам внешних файлов css, в которых и прописываются все нужные стили. При этом часто возникает необходимость в таблице стилей для каждого элемента прописывать определённый цвет (текста, фона, тени).</p>
    <p>Для того, чтобы облегчить работу по стилизации html-страниц, я написал <strong>таблицу стилей для стандартных 140 веб-цветов</strong>, применяемых в веб-дизайне и распознаваемых всеми браузерами.</p>
    <p>С помощью этой таблицы стилей 140 стандартных веб-цветов отпадает необходимость прописывать цвет для каждого html-элемента в каскадной таблице стилей. Достаточно просто подключить к html-странице <strong>таблицу стилей 140 стандартных веб-цветов</strong> <a href="../cdn/140colors/140colors.css" target="_blank">140colors.css</a> и уже в html-коде применять для элементов необходимый цвет, просто указав его название в качестве класса.</p>
    <p>Например, нам необходимо задать синий цвет фона для заголовка h2. Для этого достаточно указать для него class="blue":</p>

    <div class="w3-example">
        <h3>Пример</h3>
        <div class="w3-code htmlHigh notranslate w3-border-green">
            &lt;h2 class="blue"&gt;Заголовок на синем фоне&lt;/h2&gt;
        </div>
        <p>Результат:</p>
        <h2 class="blue">Заголовок на синем фоне</h2>
    </div>
    <p>Или задать цвет фона параграфа золотым - gold:</p>
    <div class="w3-example">
        <h3>Пример</h3>
        <div class="w3-code htmlHigh notranslate w3-border-green">
            &lt;p class="gold"&gt;Параграф из золотым фоном.&lt;/p&gt;
        </div>
        <p>Результат:</p>
        <p class="gold">Параграф из золотым фоном.</p>
    </div>
    <p>Т.е. цвет фона в данном случае указывается в качестве класса. При этом по умолчанию текст на светлых фонах отображается чёрным цветом, а на тёмных фонах - белым цветом.</p>
    <p>Также для большей контрастности и эффектности отображения белому тексту на тёмном фоне добавляется тень (как правило чёрного цвета). При этом вы можете открыть код самой таблице стилей 140 цветов и отредактировать, при необходимости поменяв и подкорректировав как цвет текста, так и цвет и толщину тени.</p>
    <p>Обратите внимание, что классы для одного элемента можно указывать через пробел, т.е., указав класс с названием цвета, также можно указывать любой другой класс, применяемый к данному элементу.</p>
