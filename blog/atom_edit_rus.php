    <h2>Как установить русский язык интерфейса в программе Atom? 100% СПОСОБ!!!</h2>

    <p>Редактор кода <strong>Атом (Atom)</strong> официально имеет только англоязычный интерфейс. Но для пользователей, которые не достаточно хорошо знают английский язык, есть специальный плагин, при установке которого появляется возможность менять интерфейс программы на нужный язык.</p>
    <p>Чтобы установить такой плагин, необходимо запустить <strong>Atom</strong>, зайти в меню <i>File</i> – <i>Settings</i> (или нажать комбинацию клавиш Ctrl + &quot;запятая&quot; на английской раскладке клавиатуры) – в открывшейся вкладке выбрать меню <i>Install</i> – затем в строке поиска ввести название плагина <strong>Atom-i18n</strong> – в первом появившемся результате нажать <i>Install</i> (т.е. установить) и данный плагин будет установлен!</p>
    <p>Сразу после установки плагина программа <strong>Атом</strong> будет русифицирована, - если ваша операционная система на русском языке. Если же по каким-либо причинам русификация не произошла сразу, то необходимо зайти в &quot;Настройки&quot; данного плагина, прокрутить чуть ниже и выбрать нужный вам язык (например, русский или украинский). Появится всплывающее окно, которое сообщит о необходимости перезагрузки <strong>Атома</strong>. Нажимаете кнопку <i>Reload</i>, и перезагружаете программу <strong>Atom</strong> с выбранным языком интерфейса.</p>
    <p>Если вам необходимо удалить данный плагин с <strong>Атома</strong>, точно также заходите через меню <i>Настройки</i> – <i>Расширения (packages)</i> – в списке <i>Пользовательские расширения</i> выбираете плагин для удаления и нажимаете <i>Удалить</i>.</p>
    <p>Хочу заметить, что в сети существует много разных сайтов, которые предлагают уже готовую русифицированную версию редактора <strong>Atom</strong>. Но лично я <b>НЕ РЕКОМЕНДУЮ</b> скачивать и устанавливать <strong>Atom</strong> с этих ресурсов, т.к. эти варианты программы могут содержать в себе дополнительный вредоносный код. Программа <strong>Атом</strong> – бесплатна и её можно скачать с официального сайта: <a href="https://atom.io/" target="_blank" rel="nofollow">https://atom.io/</a> и уже потом самостоятельно установить нужный вам язык интерфейса.</p>

