    <h2 id="c8">HTML6 Встроенная камера</h2>
    <p>HTML6 позволит нам наилучшим образом использовать камеру и мультимедиа на нашем устройстве. Мы сможем управлять камерой, её эффектами, режимами, панорамными изображениями, HDR и другими вещами.</p>
    <p>Мы могли бы использовать любые средства массовой информации и перепрофилировать их. Есть много вещей, которые можно было бы улучшить с помощью камеры и HTML6.</p>
    <hr>

    <h2 id="c9">HTML6 Хорошие микроформаты</h2>
    <p>Часто нам нужно определить нашу общую информацию в Интернете. Общей информацией может быть что угодно, например наш номер телефона, имя, адрес и т.д. Микроформаты - это стандарты, способные определять общие данные.</p>
    <p>Микроформаты могут расширить возможности дизайнера и уменьшить усилия поисковой системы, необходимые для вывода нашей общедоступной информации.</p>
    <hr>

    <h2 id="c10">HTML6 Одностраничные приложения без Javascript</h2>
    <p><strong>Bobby Mozumder</strong>, главный редактор журнала <strong>FutureClaw</strong>, предлагает:</p>
    <blockquote cite="https://morioh.com/"><i>Связывание элементов привязки с JSON / XML, конечными точками API, при которой браузер внутренне загружает данные в новую структуру данных, а затем браузер заменяет элементы DOM любыми данными, которые были загружены по мере необходимости. Исходные данные (а также стандартные ответы об ошибках) могут быть в фикстурах заголовков, которые при желании можно заменить позже.</i></blockquote>

    <p>По его словам, это шаблон веб-дизайна одностраничного приложения, который улучшит отзывчивость и время загрузки, так как не будет необходимости загружать javascript.</p>
