    <h2 id="c3">HTML6 Свобода изменения размера изображения</h2>
    <p>Эксперты полагают, что скоро появится обновление, которое позволит браузерам изменять размер изображения для лучшего просмотра.</p>
    <p>Браузеры сталкиваются с трудностями при отображении наилучшего размера изображения относительно устройства и размера окна.</p>
    <p>Теги <strong>src</strong> и <strong>img</strong> не очень эффективны для решения этой проблемы. Возможно, будет доступен новый тег **&lt;srcset&gt;**.</p>
    <p>Этот тег может облегчить браузеру выбор между более чем одним изображением для наилучшего просмотра.</p>
    <hr>

