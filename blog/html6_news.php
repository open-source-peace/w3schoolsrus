    <h2 id="c6">HTML6 Улучшение аутентификации</h2>
    <p>Хотя с точки зрения безопасности HTML5 неплох. Браузеры и веб-технологии также обеспечивают разумную защиту. Но, несомненно, в области аутентификации и безопасности можно сделать ещё намного больше.</p>
    <p>Ключи можно хранить офсайт; это предотвратит доступ нежелательных людей и усилит аутентификацию. Использование встроенных ключей вместо файлов cookie, улучшение цифровой подписи и т.д.</p>
    <p>Люди и аналитические центры могут предложить множество решений, и все зависит от того, примет или отклонит их WHATWG.</p>

    <h2 id="c7">HTML6 Индивидуальные меню</h2>
    <p>Теги <code class="w3-codespan">&lt;ul&gt;</code> и <code class="w3-codespan">&lt;ol&gt;</code> полезны, но не подходят для всех нужд. Тег <code class="w3-codespan">&lt;menu&gt;</code> мог бы лучше обрабатывать интерактивные элементы, - это необходимость времени.</p>
    <p>Тег <code class="w3-codespan">&lt;menu&gt;</code> мог бы обрабатывать элементы списка, запускаемые кнопками внутри меню.</p>

    <div class="w3-example">
        <h3>Пример:</h3>
        <div class="w3-code notranslate htmlHigh">
            &lt;menu type="toolbar"&gt;<br>
            &nbsp;&lt;li>&lt;button&gt;Вырезать&lt;/button&gt;&lt;/li&gt;<br>
            &nbsp;&lt;li>&lt;button&gt;Копировать&lt;/button&gt;&lt;/li&gt;<br>
            &nbsp;&lt;li>&lt;button&gt;Вставить&lt;/button&gt;&lt;/li&gt;<br>
            &lt;/menu&gt;
        </div>
    </div>
    <p>Тег <code class="w3-codespan">&lt;menu&gt;</code> также мог бы расширить возможности списка в HTML, и он мог бы хорошо работать, даже как обычный список.</p>
