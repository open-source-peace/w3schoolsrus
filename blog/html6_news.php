    <hr>

    <h2 id="c5">HTML6 Аннотации к изображениям и видео</h2>
    <p>Было бы здорово, если бы мы могли аннотировать изображения и видео в HTML. HTML5 дает нам возможность интерпретировать слова, предложения и абзацы, но не изображения и видео.</p>
    <p>Многие организации в этой сфере предложили решения, и похоже, что WHATWG рассмотрит хотя бы некоторые из них.</p>
    <p>Будем надеяться, что скоро мы будем добавлять аннотации к изображениям и видео в HTML.</p>
