    <h2 id="c1">HTML6 Экспресс-теги</h2>
    <p>Как следует из названия, эти теги являются экспресс-тегами. Можно использовать такие теги, как логотип на вашей веб-странице. Кроме того, будут доступны такие теги, как ,, (запятые).
    <hr>

    <h2 id="c2">HTML6 Поддержка собственных модалов</h2>
    <p>Элемент <strong>dialog</strong> находится в разработке с HTML6. Этот элемент считается эквивалентом модальных окон на базе JavaScript.</p>
    <p>Элементы <strong>dialog</strong> уже стандартизированы, но только некоторые браузеры, такие как Chrome и Интернет-браузеры Samsung, имеют полную поддержку. Не беспокойтесь, скоро он будет работать во всех браузерах.</p>
    <p>Этот элемент в формате по умолчанию будет отображаться только при наведении курсора на то место, где он был размещен.</p>
    <p>Чтобы использовать более распространенный интерфейс, подобный модальному, вы можете открыть модальный метод с помощью JavaScript.</p>

    <div class="w3-example">
        <h3>Пример:</h3>
        <div class="w3-code notranslate htmlHigh">
            &lt;dialog&gt;<br>
            &nbsp;&lt;form method="dialog"&gt;<br>
            &nbsp;&nbsp;&lt;input type="submit" value="Ok" /&gt;<br>
            &nbsp;&nbsp;&lt;input type="submit" value="Cancel" /&gt;<br>
            &nbsp;&lt;/form&gt;<br>
            &lt;/dialog&gt;
        </div>
    </div>

    <p>В форме по умолчанию элемент создает серый фон с неинтерактивным содержимым под ним.</p>
    <p>Атрибут <code class="w3-codespan">method</code> может использоваться в формах внутри элемента <code class="w3-codespan">&lt;dialog&gt;</code>. Это отправит значение и вернет значение самому объекту <code class="w3-codespan">&lt;dialog&gt;</code>.</p>
    <p>В целом, этот элемент <code class="w3-codespan">&lt;dialog&gt;</code> полезен при небольшом взаимодействии с пользователем и улучшении пользовательского интерфейса. Вы можете переключить атрибут <code class="w3-codespan">open</code>, чтобы открыть и закрыть его в стандартной HTML-форме.</p>
    <hr>

    <div class="w3-example">
        <h3>Пример:</h3>
        <div class="w3-code notranslate htmlHigh">
            &lt;dialog open&gt;<br>
            &nbsp;&nbsp;&lt;p&gt;Диалоговое окно, созданное с помощью HTML6&lt;/p&gt;<br>
            &lt;/dialog&gt;
        </div>
    </div>
