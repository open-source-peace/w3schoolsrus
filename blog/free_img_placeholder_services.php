    <p>(Последнее обновление статьи было 23 мая 2018 г.)</p>
    <p>Когда вы создаете шаблоны веб-сайтов, пользовательские интерфейсы или первоначальные прототипы приложений, часто возникает необходимость в заполнителях или фиктивных изображениях. Вместо того, чтобы тратить время и силы на поиск и обрезку пользовательских изображений, вы можете просто использовать бесплатный сервис-заполнитель изображений, который автоматически создаст для вас изображение с указанными вами размерами. Вот некоторые из лучших бесплатных сервисов-заполнителей изображений.</p>

    <h3><a href="https://placeholder.com/" target="_blank" rel="nofollow">Placehold.it</a></h3>
    <p>Чистый, простой и отшлифованный сервис. Если вы ищете минималистичный подход, то это подходящий вариант, так как вместо изображений используются простые серые поля, например:</p>

    <div class="w3-code htmlHigh notranslate w3-border-green">
        &lt;img src="http://placehold.it/350x150" /&gt;
    </div>

    <h3><a href="http://lorempixel.com/" target="_blank" rel="nofollow">Lorempixel.com</a></h3>
    <p>Эта служба предоставляет фактические изображения, которые были классифицированы для вас, чтобы вы могли отображать изображения-заполнители, соответствующие теме вашего веб-сайта, например, спорт:</p>

    <div class="w3-code htmlHigh notranslate w3-border-green">
        &lt;img src="http://lorempixel.com/400/200/sports/" /&gt;
    </div>

    <h3><a href="http://flickholdr.com/" target="_blank" rel="nofollow">Flickholdr</a></h3>
    <p>Это довольно умный сервис-заполнитель изображений, так как он получает изображения из flickr на основе тегов. Это делает его отличным вариантом для добавления изображений, связанных с вашим сайтом, например:</p>

    <div class="w3-code htmlHigh notranslate w3-border-green">
        &lt;img src="http://flickholdr.com/200/300/sea,sun" /&gt;
    </div>

    <h3><a href="https://dummyimage.com/" target="_blank" rel="nofollow">Dummy Image Generator</a></h3>
    <p>Этот вариант очень настраиваемый, так как вы можете изменить цвет, добавить текст и даже изменить формат файла изображения. Он не выглядит лучше, но обладает отличной функциональностью, например:</p>

    <div class="w3-code htmlHigh notranslate w3-border-green">
        &lt;img src="http://dummyimage.com/600x400/000/fff"  /&gt;
    </div>

    <h3><a href="http://placekitten.com/" target="_blank" rel="nofollow">Place Kitten</a></h3>
    <p>Определенно самый симпатичный сервис-заполнитель изображений. Множество котят готовы к использованию в качестве изображений-заполнителей в ваших проектах или коде, например.</p>

    <div class="w3-code htmlHigh notranslate w3-border-green">
        &lt;img src="http://placekitten.com/200/300" /&gt;
    </div>
